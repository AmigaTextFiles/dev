org $30000
load $30000
clrscr: 	MACRO
	movem.l	d0/d1/a5,-(a7)
	lea	$dff000,a5
mc_wait?0:
	btst	#14,$02(a5)
	bne.s	mc_wait?0
	move.l	?1,$54(a5)
	move.w	?2,d0       ;x
	move.w	?3,d1       ;y
	move.w	?4,$66(a5)
	move.w	#%0000000100000000,$40(a5)
	move.w	#0,$42(a5)
	lsl.w	#6,d1
	or.w	d1,d0
	move.w	d0,$58(a5)
	movem.l	(a7)+,d0/d1/a5
	ENDM




clear: 	MACRO
	lea	$dff000,a5
clearwait?0:
	btst	#14,2(a5)
	bne.s	clearwait?0
	move.l	?1,d0
	addq.l	#4,d0
	move.l	d0,$54(a5)
	move.w	?2,d0       ;x
	move.w	?3,d1       ;y
	move.w	?4,$66(a5)
	move.w	#%0000000100000000,$40(a5)
	move.w	#0,$42(a5)
	lsl.w	#6,d1
	or.w	d1,d0
	move.w	d0,$58(a5)
	ENDM

m:
	
 		lea	$dff000,a5
		movem.l	d0-d7/a0-a6,-(a7)
		moveq	#61,d0
		lea	zero,a0
		lea	8,a1
copyzero:	move.l	(a1)+,(a0)+
		dbf	d0,copyzero
		move.l	#raus,$020
raus:		move.w	#$2700,sr
		adda	#6,a7
		move.l	a7,sstack
		move.l	#stackend,a7
		move.w	$1c(a5),intbuff
		move.w	#$7fff,$9a(a5)
		move.w	#$7fff,$9c(a5)
		bra.L	main		
oson:		move.w	#$2700,sr
		move.w	#$7fff,$9a(a5)
		move.w	intbuff(pc),d0
		ori.w	#$8000,d0
		move.w	d0,$9a(a5)
		moveq	#61,d0
		lea	zero(pc),a0
		lea	8,a1
zeroback:	move.l	(a0)+,(a1)+
		dbf	d0,zeroback
		move.l	sstack(pc),a7
		move.w	#0,sr
		movem.l	(a7)+,d0-d7/a0-a6
		rts
grname:		dc.b	"graphics.library",0
		align	4

zero:		blk.l	62,0
		align	4
sstack:		dc.l	0
stack:		blk.b	4048
stackend:
		align	4

intbuff:	dc.l	0
		align	4


main:		
**********************
*** Voreinstellung ***
**********************
	bsr	objadd
	lea	$70000,a0
	move.l	#$ffff,d0
spclear:
	move.b	#0,(a0)+
	dbf	d0,spclear


	lea	$dff000,a5
	move.l	#$ffffffff,$44(a5)
	move.w	#%1000010000000000,$96(a5)
	move.w	#%0000011111111111,$96(a5)
	move.l  #copperlist,$80(a5)
	move.w  #%1000011111100000,$96(a5)
	lea	hauptloop(pc),a4
	move.l	a4,$6c		
	move.w	#$c010,$9a(a5)
	move.w	#$2000,sr

************
*** Wait ***
************

wait:	
	btst	#6,$bfe001
	beq	end
swait:
	move.l	$dff004,d0
	and.l	#$1ff00,d0
	cmp.l	#$12900,d0
	ble	swait
	bsr	filledvectors
	move.w	#$2700,sr
	move.l	showplane,a0
	move.l	workplane,showplane
	move.l	a0,workplane
	move.w	#$2000,sr
	bra	wait		




**************************
******	Hauptteil  *******
**************************

hauptloop:
	move.w	#$10,$dff09c		
	movem.l	d0-d7/a0-a6,-(a7)
	moveq	#3,d7
	lea	planeadrs,a0
	move.l	showplane,d0
setplanes:
	swap	d0
	move.w	d0,2(a0)
	swap	d0
	move.w	d0,6(a0)
	add.l	#8,a0
	add.l	#150*40,d0
	dbf	d7,setplanes
	add.w	#1,speedpointer
hplend:
	movem.l	(a7)+,d0-d7/a0-a6
	rte				
workplane:
	dc.l	$7a240
showplane:
	dc.l	$74480

speedpointer:
	dc.w	0
speed:
	dc.w	1


*************************************************
;This routine adds the Lableadresses to all 
;adresses in the objects.
;Insert the Load-Lables after -objectsaddtable-!!!

Objectsaddtable:
	dc.l	obj1coordstable
	dc.l	$ffffffff

objadd:
	lea	objectsaddtable(pc),a0
objadd1:
	move.l	(a0)+,d0
	cmp.l	#$ffffffff,d0
	beq.s	objaddend
	lea	1024,a2
	add.l	d0,a2
objadd2:
	cmp.l	#$ffffffff,(a2)+
	beq.s	objadd1
	add.l	d0,-4(a2)
	move.l	-4(a2),a3
objadd3:
	cmp.l	#$ffffffff,(a3)+
	beq.s	objadd2
	add.l	d0,-4(a3)	
	bra	objadd3
objaddend:
	rts

**************************************************

Filledvectors:
	movem.l	d0-d7/a0-a6,-(a7)
	clrscr	workplane,#20,#600,#0
	bsr	vectorloop
	move.w	speedpointer,speed
	clr.w	speedpointer	
	movem.l	(a7)+,d0-d7/a0-a6
	rts

***********************
*****  Vectorloop *****
***********************

vectorloop:
	movem.l	d0-d7/a0-a6,-(a7)
	lea	obj1table,a6
	lea	obj1pos,a0
	add.w	#26,12(a0)
	add.w	#30,14(a0)
	add.w	#38,16(a0)
	and.w	#$1fff,12(a0)
	and.w	#$1fff,14(a0)
	and.w	#$1fff,16(a0)
	bsr	coordscalc
	bsr	d2coordscalc
	bsr	ftesten
	bsr	objzeichnen
	movem.l	(a7)+,d0-d7/a0-a6
	rts
*********************
***   Coordscalc  ***
*********************
coordscalc:
	move.l	4(a6),a3
	move.l	12(a6),a4
	move.l	28(a6),d0
	move.l	(a4,d0),a4
	move.l	16(a6),a5

coordscalcloop:
	move.w	(a4),d0
	cmp.w	#$7fff,d0
	beq	coordscalcloopend
	move.w	4(a4),d1
	move.w	8(a4),d2
	sub.w	24(a3),d0
	sub.w	26(a3),d1
	sub.w	28(a3),d2
	move.w	12(a3),a0
	move.w	14(a3),a1
	move.w	16(a3),a2
	bsr	rotate
	add.w	24(a3),d0
	add.w	26(a3),d1
	add.w	28(a3),d2
	add.w	(a3),d0
	add.w	2(a3),d1
	add.w	4(a3),d2

	move.w	d0,(a5)+
	move.w	d1,(a5)+
	move.w	d2,(a5)+
	move.w	12(a4),(a5)+
	add.l	#14,a4
	bra	coordscalcloop
coordscalcloopend:
	move.w	#$7fff,(a5)
	rts

********************
*** 2dcoordscalc ***
********************

d2coordscalc:
	move.l	16(a6),a4
	move.l	20(a6),a5
d2coordscalcloop:
	move.w	(a4)+,d0
	cmp.w	#$7fff,d0
	beq	d2coordscalcend
	move.w	(a4)+,d1
	move.w	(a4)+,d2
	ext.l	d0
	ext.l	d1
	ext.l	d2
	asl.l	#8,d0
	asl.l	#8,d1
	divs	d2,d0
	divs	d2,d1
	add.w	#75,d1
	add.w	#159,d0
	move.w	d0,(a5)
	move.w	d1,2(a5)
	move.w	(a4)+,4(a5)
	add.l	#8,a5
	bra	d2coordscalcloop
d2coordscalcend:
	move.w	#$7fff,(a5)
	rts

***************
*** Ftesten ***
***************
;   a4  ftable    a5  3dcoords

ftesten:
	move.l	16(a6),a5
	move.l	12(a6),a4
	move.l	28(a6),d0
	move.l	4(a4,d0),a4
ftestloop:
	move.l	(a4)+,a0
	cmp.l	#$ffffffff,a0
	beq.s	ftestloopend
	move.l	(a0),a0
	move.l	a5,a1
	bsr	sichttest
	bra	ftestloop
ftestloopend:
	rts


*******************
*** OBJzeichnen ***
*******************	
; a4  ftable    a5  2dcoords
objzeichnen:
	move.l	12(a6),a4
	move.l	28(a6),d0
	move.l	4(a4,d0),a4
	move.l	20(a6),a5	
objzloop:
	move.l	(a4)+,a2
	cmp.l	#$ffffffff,a2
	beq.s	objzloopend
objzloop2:
	move.l	(a2)+,a0
	cmp.l	#$ffffffff,a0
	beq.s	objzloop
	tst.w	(a0)
	beq.s	objzloop2
	move.l	a5,a1
	bsr	fill
	bra	objzloop2
objzloopend:
	rts

*****************
*** Sichttest ***
*****************
;a0   coordstable
;a1   coords

sichttest:
	movem.l	d0-d7/a0-a6,-(a7)
	move.w	6(a0),d7
	lsl.w	#3,d7
	move.w	(a1,d7.w),d0
	move.w	2(a1,d7.w),d1
	move.w	4(a1,d7.w),d2
	asr.w	#1,d0
	asr.w	#1,d1
	asr.w	#1,d2
	move.w	d0,a3
	move.w	d1,a4
	move.w	d2,a5
	move.w	2+6(a0),d7
	lsl.w	#3,d7
	move.w	(a1,d7.w),d0
	move.w	2(a1,d7.w),d1
	move.w	4(a1,d7.w),d2
	asr.w	#1,d0
	asr.w	#1,d1
	asr.w	#1,d2
	move.w	4+6(a0),d7
	lsl.w	#3,d7
	move.w	(a1,d7.w),d3
	move.w	2(a1,d7.w),d4
	move.w	4(a1,d7.w),d5
	asr.w	#1,d3
	asr.w	#1,d4
	asr.w	#1,d5
	sub.w	a3,d0	;v
	sub.w	a4,d1
	sub.w	a5,d2
	sub.w	a3,d3	;w
	sub.w	a4,d4
	sub.w	a5,d5
	move.w	d0,vx+2
	move.w	d0,vx2+2
	move.w	d1,vy+2
	move.w	d1,vy2+2
	move.w	d2,vz+2
	move.w	d2,vz2+2
	move.w	d3,d0
	move.w	d4,d1
	move.w	d5,d2
vy:
	muls	#0,d5
vz:
	muls	#0,d1
	sub.w	d1,d5
			;x
vz2:
	muls	#0,d3
vx:
	muls	#0,d2
	sub.w	d2,d3
			;y
vx2:
	muls	#0,d4
vy2:
	muls	#0,d0
	sub.w	d0,d4
			;z
	move.w	a3,d0
	move.w	a4,d1
	move.w	a5,d2

	muls	d0,d5
	muls	d1,d3
	muls	d2,d4
	add.l	d3,d4
	add.l	d4,d5
	tst.l	d5
	bge.s	unsich
sich:
	move.w	#0,(a0)
	bra.s	sichtend
unsich:
	move.w	#1,(a0)
sichtend:
	movem.l	(a7)+,d0-d7/a0-a6
	rts


*************************
**** Flaechenfuellen ****
*************************
;a0 Tabelle mit Coordszeiger
;a1 Coords


fill:
	movem.l	d0-d7/a2-a6,-(a7)
	clrscr	#$72d10,with,high,modu
	move.w	2(a0),d7
	move.w	d7,coordsanzahl
	clr.w	fcolor
	move.b	5(a0),fcolor+1
	subq.w	#1,d7
	addq.l	#6,a0
	move.w	#$7fff,xminwert
	move.w	#$7fff,yminwert
	clr.w	xmaxwert
	clr.w	ymaxwert

	lea	fillcoordstable(pc),a5
makefillcoordstable:
	move.w	(a0)+,d0
	lsl.w	#3,d0
	move.l	a0,a3
	sub.l	a6,a3
	move.w	(a1,d0.w),d1
	move.w	2(a1,d0.w),d2
	cmp.w	xminwert(pc),d1
	bge.s	xminset
	move.w	d1,xminwert
xminset:
	cmp.w	yminwert(pc),d2
	bge.s	yminset
	move.w	d2,yminwert
yminset:
	cmp.w	xmaxwert(pc),d1
	ble.s	xmaxset
	move.w	d1,xmaxwert
xmaxset:
	cmp.w	ymaxwert(pc),d2
	ble.s	ymaxset
	move.w	d2,ymaxwert
ymaxset:
	move.w	d1,(a5)+
	move.w	d2,(a5)+
	dbf	d7,makefillcoordstable


	lea	fillcoordstable(pc),a2
	move.w	coordsanzahl(pc),d7
	subq.w	#2,d7
	move.w	xminwert(pc),d4
	move.w	yminwert(pc),a5
	lea	$72d10,a0
	lea	40,a1
filldraw:
	move.w	(a2)+,d0
	move.w	(a2)+,d1
	move.w	(a2),d2
	move.w	2(a2),d3	
	sub.w	d4,d0
	sub.w	d4,d2
	sub.w	a5,d1
	sub.w	a5,d3
	cmp.w	d1,d3
	beq	fillend
	cmp.w	d1,d3
	bge.s	nochange
	exg	d1,d3
	exg	d0,d2
nochange:
	move.w	d2,d5
	move.w	d3,d6
	sub.w	d0,d5
	bpl.s	nomin
	neg.w	d5
nomin:
	sub.w	d1,d6
	asl.w	#1,d6
	cmp.w	d5,d6
	bgt.s	nomore
	subq.w	#1,d3
nomore:
	bsr	linedraw
fillend:
	dbf	d7,filldraw

	move.w	xmaxwert(pc),d2
	move.w	ymaxwert(pc),d3
	sub.w	d4,d2
	sub.w	a5,d3
	addq.w	#1,d3
	cmp.w	#2,d3
	blt	allfend
	moveq	#0,d1
	lsr.w	#4,d2
	addq.w	#1,d2
	move.w	d2,d1
	lsl.w	#1,d1
	moveq	#40,d7
	sub.w	d1,d7
	move.w	d2,with
	move.w	d3,high
	move.w	d7,modu
	lea	$dff000,a5
blittfillwait:
	btst	#14,2(a5)
	bne.s	blittfillwait
	move.l	#$ffffffff,$44(a5)
	move.w	d7,$64(a5)
	move.w	d7,$66(a5)
	move.w	d3,d4
	mulu	#40,d4
	add.l	d1,d4
	sub.l	#82,d4
	add.l	#$72d10,d4
	move.l	d4,$50(a5)
	move.l	d4,$54(a5)
	move.w	#%0000100111110000,$40(a5)
	move.w	#%1010,$42(a5)
	subq.w	#1,d3
	lsl.w	#6,d3
	or.w	d3,d2
	move.w	d2,$58(a5)
fillwait:
	btst	#14,2(a5)
	bne.s	fillwait
	move.w	xminwert(pc),d0
	move.w	yminwert(pc),d1
	mulu	#40,d1
	move.w	d0,d2
	and.w	#$fff0,d0
	lsr.w	#3,d0
	add.w	d0,d1	

	add.l	workplane,d1

	and.w	#$f,d2
	ror.w	#4,d2
	move.w	with(pc),d3
	move.w	high(pc),d4
	addq.w	#1,d3
	lsl.w	#6,d4
	or.w	d4,d3	
	move.w	modu,d0
	subq.w	#2,d0
	move.w	d0,$64(a5)
	clr.w	$62(a5)
	move.w	d0,$66(a5)
	move.w	d0,$60(a5)
	move.w	fcolor(pc),d6	
	moveq	#3,d7
fcopyloop:
	move.l	d1,$54(a5)
	move.l	d1,$48(a5)
	move.l	#$72d10,$50(a5)
	btst	#0,d6
	beq.s	zeroplane	
	move.w	#%101111111010,d0	
	bra.s	zeroend
zeroplane:
	move.w	#%101100001010,d0	
zeroend:
	lsr.w	#1,d6
	or.w	d2,d0
	move.w	#0,$42(a5)
	move.w	d0,$40(a5)
	move.w	d3,$58(a5)
copywait:
	btst	#14,2(a5)
	bne.s	copywait
	add.l	#150*40,d1
	dbf	d7,fcopyloop

allfend:

	movem.l	(a7)+,d0-d7/a2-a6
	rts




fcolor:
	dc.w	0


coordsanzahl:
	dc.w	0
high:
	dc.w	200
with:
	dc.w	20
modu:
	dc.w	0
xmaxwert:
	dc.w	0
xminwert:
	dc.w	$7fff
ymaxwert:
	dc.w	0
yminwert:
	dc.w	$7fff
fillcoordstable:
	blk.w	400,0



********************
*** 3-D Rotieren ***
********************
;d0,d1,d2   x,y,z
;a0,a1,a2   umx,umy,umz

rotate:
	movem.l	d3-d7/a3-a6,-(a7)
	lea	costable(pc),a3
	lea	sintable(pc),a4
	move.w	d0,d4
	move.w	d1,d5
	muls	(a3,a2.w),d4
	muls	(a4,a2.w),d5
	asr.l	#8,d4
	asr.l	#8,d5
	asr.l	#7,d4
	asr.l	#7,d5
	sub.w	d5,d4
	move.w	d4,d6
	move.w	d0,d4
	move.w	d1,d5
	muls	(a4,a2.w),d4
	muls	(a3,a2.w),d5
	asr.l	#8,d4
	asr.l	#8,d5
	asr.l	#7,d4
	asr.l	#7,d5
	add.w	d4,d5
	move.w	d5,d1
	move.w	d6,d0
rot_1:
	move.w	d2,d5
	move.w	d1,d4
	muls	(a3,a0.w),d4
	muls	(a4,a0.w),d5
	asr.l	#8,d4
	asr.l	#8,d5
	asr.l	#7,d4
	asr.l	#7,d5
	sub.w	d5,d4
	move.w	d4,d6
	move.w	d1,d4
	move.w	d2,d5
	muls	(a4,a0.w),d4
	muls	(a3,a0.w),d5
	asr.l	#8,d4
	asr.l	#8,d5
	asr.l	#7,d4
	asr.l	#7,d5
	add.w	d4,d5
	move.w	d5,d1
	move.w	d6,d2
rot_2:
	move.w	d0,d4
	move.w	d2,d5
	muls	(a3,a1.w),d4
	muls	(a4,a1.w),d5
	asr.l	#8,d4
	asr.l	#8,d5
	asr.l	#7,d4
	asr.l	#7,d5
	add.w	d5,d4
	move.w	d4,d6
	move.w	d0,d4
	move.w	d2,d5
	neg.w	d4
	muls	(a4,a1.w),d4
	muls	(a3,a1.w),d5
	asr.l	#8,d4
	asr.l	#8,d5
	asr.l	#7,d4
	asr.l	#7,d5
	add.w	d4,d5
	move.w	d5,d2
	move.w	d6,d0
rot_end:
	movem.l	(a7)+,d3-d7/a3-a6
	rts


***********************
**** Blitterlinien ****
***********************
;a1 Breite   a0 ADR

linedraw:
	movem.l	d4-d7/a2-a6,-(a7)
	cmp.w	d0,d2
	bne.s	li_weiter
	cmp.w	d1,d3
	beq	li_end
li_weiter:
	cmp.w	#0,d0
	blt	li_end
	cmp.w	#0,d1
	blt	li_end
	cmp.w	#0,d2
	blt	li_end
	cmp.w	#0,d3
	blt	li_end
	lea	$dff000,a5
	move.l	a1,d4
	mulu	d1,d4
