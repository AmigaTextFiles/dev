*******************************************************************************
*OPEN WINDOW SCREEN
*******************************************************************************
WINDOPEN:
	MOVE.L	INTBASE,A6	* INTUITION LIBRAY BASE ADRESS
	LEA	WINDOW,A0	* WINDOW START ADRESS
	JSR	OPENWINDOW(A6)	* OPENWINDOW LIBRARY OFFSET
	MOVE.L	D0,WINDOWADRESS	* STORE SCREEN START ADRESS
	RTS

WINDCLOSE:
	MOVE.L	INTBASE,A6	* FIND WINDOW START ADRESS THEN
	MOVE.L	WINDOWADRESS,A0	* CALL SYSTEM TO CLOSE WINDOW
	JSR	CLOSEWINDOW(A6)
	RTS
*******************************************************************************
WINDOWADRESS:	DC.L	0	* WINDOW START ADRESS PIONTER
*******************************************************************************
WINDOW:
XPOS:	DC.W	0
YPOS:	DC.W	0
WIDTH	DC.W	1
HEIGHT	DC.W	1
PCOLR	DC.B	1		* PRINT COLOR
BCOLR	DC.B	3		* BACKGROUNDCOLOR	
ICDMP:	DC.L	MENUPICK 	*WHAT EVENTS INTUITION SHOULD REPORT
ACTI:	DC.L	$100F		* ACTIVATE ALL GADGETS
GAD:	DC.L	0		* FIRST GADGET	
CMARK:	DC.L	0	
NAME:	DC.L	WINDOWNAME
SCREENA:DC.L	0		* SCREEN BASE ADRESS POINTER
BMAP:	DC.L	0
SWIDTH:	DC.W	300		* SMALLEST WIDTH
SHIEGHT:DC.W	10		* SMALLEST HEIGHT
MWIDTH:	DC.W	320		* MAX WIDTH
MHIEGHT:DC.W	10		* MAX HEIGHT
STYPE:	DC.W	CUSTOMSCREEN	* SCREEN TYPE
WINDOWNAME:
	DC.B	"BOOT MENU V1.0",0	* WINDOW TITLE
	EVEN
