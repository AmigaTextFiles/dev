*******************************************************************************
*OPEN WINDOW SCREEN
*******************************************************************************
MENUTEXT:
	MOVE.L	INTBASE,A6		* INTUITION LIBRAY BASE ADRESS
	LEA	STRINGWINDOW,A0		* WINDOW START ADRESS
	JSR	OPENWINDOW(A6)		* OPENWINDOW LIBRARY OFFSET
	MOVE.L	D0,SWINDOWADRESS	* STORE STRING WINDOWSTART ADRESS
	
NOCLOSE:	
	MOVE.L	$4,A6			* EXECBASE ADRESS
	MOVE.L	SWINDOWADRESS,A0	* STRING WINDOW START ADRESS
	MOVE.L	$56(A0),A0		* INTUITION USER PORT
	JSR	GETMSG(A6)		* LIB CALL DID ANYTHING HAPPEN
	TST.L	D0			* 0 NO ANYTHING ELSE YES
	BEQ	NOCLOSE			* ZERO THEN BRANCH BACK
	MOVE.L	D0,A0			* MOVE UPDATED USER PORT
	MOVE.L	$14(A0),D6		* USER PORT + 20
	CMP.W	#CLOSEWINDOWW,D6	* COMPARE VALUES ($200)
	BNE	NOCLOSE			* IF EQUAL CLOSE WINDOW ELSE
					* BRANCH AND CARRY ON TESTING

	MOVE.L	INTBASE,A6		* INTUITION BASE ADRESS
	MOVE.L	SWINDOWADRESS,A0	* WINDOW BASE ADRESS
	JSR	CLOSEWINDOW(A6)		* LIBRARY CALL TO CLOSE WINDOW
	RTS				* RETURN TO MAIN PRG
