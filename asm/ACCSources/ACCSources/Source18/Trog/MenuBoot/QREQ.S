*******************************************************************************
*REQUESTER TO QUIT PROGRAM OR NOT
*******************************************************************************
REQUEST:MOVE.L	INTBASE,A6	*INTUTION BASE ADRESS IN A6
	MOVE.L	#0,D0		*CLEAR CHECK REGISTERS (D0, D1)
	MOVE.L	#0,D1
	MOVE.L	#220,D2		*REQUESTER WIDTH
	MOVE.L	#60,D3		*REQUESTER HEIGHT
	MOVE.L	WINDOWADRESS,A0	*ATTACH REQUESTER TO THIS WINDOW
	LEA	BTEXT,A1	*POINTER TO MAIN REQUESTER TEXT
	LEA	LTEXT,A2	*TEXT DISPLAYED ON LEFT
	LEA	RTEXT,A3	*TEXT DISPLAYED ON RIGHT
	JSR	AUTOREQUEST(A6)	*AUTOREQUEST()
	MOVE.W	D0,QFLAG
	RTS
*******************************************************************************	
*TEXT FOR THE REQUESTER AS WELL AS COLOR AND POSTION
*******************************************************************************
*POINTERS AND TEXT DETAILS
*******************************************************************************
QFLAG:	DC.W	0			*STORE FOR RESULT FROM REQUESTER QUIT

BTEXT	DC.B	0,1			*TEXT COLOR, BACKGROUND COLOR
	DC.B	0			*OUTPUT MODE 0=NORMAL 4=INVERTED
	EVEN		
	DC.W	10,10			*X AND Y POSTION OF TEXT
	DC.L	0			*FONT SET DETAILS 0=NORMAL
	DC.L	BODYTXT			*POINTER TO TEXT TO OUTPUT
	DC.L	0			*POINTER TO MORE TEXT 0=NO MORE TEXT
BODYTXT	DC.B "DO YOU REALLY WANT TO",0	*TEXT TO BE OUTPUT ON REQUESTER
	
	EVEN

LTEXT	DC.B	0,1			*TEXT COLOR, BACKGROUND COLOR
	DC.B	0			*OUTPUT MODE 0=NORMAL 4=INVERTED
	EVEN		
	DC.W	5,3			*TEXT POSTION
	DC.L	0			*NORMAL FONT
	DC.L	LEFTTXT			*POINTER TO TEXT TO BE OUTPUT
	DC.L	0			*NO MORE TEXT
LEFTTXT	DC.B	"YES",0			*TEXT TO BE OUTPUT TO REQUESTER
	
	EVEN
	
RTEXT	DC.B	0,1			*TEXT COLOR, BACKGROUND COLOR
	DC.B	0			*OUTPUT MODE 
	EVEN
	DC.W	5,3			*TEXT POSTION
	DC.L	0			*NO MORE TEXT
	DC.L	RIGHTTX			*POINTER TO TEXT TO BE OUTPUT
	DC.L	0			*NO MORE TEXT 0=NO MORE TEXT
RIGHTTX	DC.B	"NO",0			*TEXT TO BE OUTPUT TO REQUESTER
	
	EVEN
