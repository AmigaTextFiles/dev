	SECTION	CRYPTER,CODE

**********

CRYPTER	LEA	TEXT,A4			TEXT ADDRESS --> A4
	MOVEQ.L	#0,D0			CLEAR D0
NOTFIN	MOVE.B	(A4),D0			READ FIRST CHARACTER
	CMPI.B	#120,D0			IS IT AND ENCHAR (x) ?
	BEQ.S	SETFLG			YES? SET NOTFLAG
	NOT.B	D0			THIS IS THE ENCRYPTION!
	MOVE.B	D0,(A4)+		WRITE NEW CHAR BACK
	CMPI.L	#$F,NOTFLAG		WAS THAT THE LAST?
	BNE.S	NOTFIN			NO? CONTINUE
	MOVEQ.L	#0,D0			WE DON`T WANT AN ERROR!
	RTS				AUF WIEDERSEHEN!

**********

SETFLG	MOVE.L	#$F,NOTFLAG
	RTS

**********

TEXT	DC.B 'HELLO THERE! USE MONAM TO SEE HOW THIS TEXT IS ENCRYPTED! x'
	EVEN

NOTFLAG	DC.B	0

