	move.w	d0,6(a0)		;UPDATE COPPER
	swap	d0
	move.w	d0,2(a0)
	swap	d0	
	move.l	#Sprite0,a0		;A0=SPRITE 0 ADDRESS
	move.l	#Sprite1,a1		;A1=SPRITE 1 ADDRESS
	move.w	#10,d7			;NO OF SPRITES-1
Move
	addq.b	#3,1(a0)		;ADD #3 TO HORIZONTAL POSITIONAL BYTE
	addq.b	#3,1(a1)		;SAME FOR SPRITE 1
	add.l	#68,a0			;LOCATE NEXT PAIR OF CO-ORDINATES
	add.l	#68,a1			;"    "
	dbf	d7,Move			;DECREMENT AND BRANCH WHEN =0
	rts
