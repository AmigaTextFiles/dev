
; Listing9b2.s
; data manipulation

 1. W <addr> <values[.x]				separated by space> Write into Amiga memory.
 2. W <addr> 'string'					Write into Amiga memory.
 3. Wf <addr> <endaddr> <bytes or string like above>, fill memory.
 4. Wc <addr> <endaddr> <destaddr>		, copy memory.
 5. r <reg> <value>						Modify CPU registers (Dx,Ax,USP,ISP,VBR,...).

