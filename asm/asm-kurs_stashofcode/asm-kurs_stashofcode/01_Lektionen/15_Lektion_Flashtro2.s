ALTE CRACKTROS STERBEN NIE: INTERVIEW MIT DEM SCHÖPFER VON FLASHTRO

2. September 2017 Amiga

Jeder hat seine Madeleine Proust, das kleine Nichts, das ihn jeden Tag aufs
Neue in die verzauberte Welt seiner Jugend zurückversetzt. Für manche ist es
das "Cracktro", das man in den 80er oder 90er Jahren auf ihren 8-Bit- oder
16-Bit-Maschinen, Computern oder Konsolen gesehen hat.

Wenn Sie keinen Computer haben, ist die Verwendung eines Emulators nicht die
einzige Möglichkeit, dieses Erbe wiederzubeleben: Enthusiasten portieren es auf
Flash oder HTML5. Treffen mit Musashi, dem Schöpfer von Flashtro, einer
Referenzseite des Genres.

Die Flashtro-Website

"LEGENDEN STERBEN NIE"

Am 9. Juli 2003 veröffentlichte Bacchus, einer der Gründer der mythischen
Gruppe Fairlight (kurz FLT), eine Nachricht auf einer Website für Commodore 64-
Enthusiasten, in der er den Tod der Gruppe bestritt, der durch Gerüchte in 
einer im Vormonat ausgestrahlten Nachricht angekündigt wurde - wahrscheinlich
als Folge der wenige Tage zuvor erfolgten Verurteilung eines Mitglieds von
Razor 1911 zu 18 Monaten Gefängnis - unterzeichnete er unter Verwendung des
bekannten Mottos von Fairlight: "Legends never die".
Fairlight war damals nicht nur für seine Cracks bekannt, sondern auch für seine
(seltenen) Demos und (vielen) Cracktros, die von seiner eigenen Abteilung von
Demomakern oder Drittanbietern hergestellt wurden. Über die Ritzen hinweg 
gesehen und begutachtet, konnten die Cracktros umso mehr Eindruck hinterlassen,
als sie genau dieses Ziel verfolgten.
Der grafische Effekt, der beim Start des Spiels ausgeführt wurde, ermöglichte
es der Gruppe in Kombination mit einem Musikstück, ihren Crack effektiv zu
behaupten und alle möglichen Informationen zu kommunizieren, einschließlich
Möglichkeiten, sie zu kontaktieren, und Grüße an Mitglieder der "Szene".
Auf Amiga, dem 16-Bit-Commodore, auf dem Fairlight zwischen Ende der 80er und
Mitte der 90er Jahre besonders wütete, war die Gruppe nicht allein aktiv. Es
konkurrierte mit vielen anderen wie Skid Row, Quartex, Crystal usw. um seine
führende Position in der Szene. So viele Gruppen, so viele Cracktro-
Produktionen, die Gruppen versuchen auch oft, sich in diesem Genre zu erneuern
und so viele Erinnerungen, an die sich Cracktro-Enthusiasten heute erinnern
werden.
So Musashi, der Schöpfer der Flashtro-Site. Auf seiner Seite kann jeder in
seine Proust Madeleine beißen. Tatsächlich gibt es Dutzende von Cracktros für
alle Maschinen: Amiga, Atari ST, Commodore 64, Dreamcast, Game Boy Advance,
Nintendo 64 usw. Zunächst portierte Musashi diese Cracktros auf Flash, dann
wechselte er zu HTML5. Ein Werk, das ein erstaunlich originalgetreues Ergebnis
hervorbringt, das von den Autoren der Cracktros gefeiert wird, wenn sie dort
nicht ohne Stolz feststellen, dass ihre Produktionen weiterhin geschätzt 
werden. Dank Flastro sterben "alte Crackros nie"!

EIN INTERVIEW MIT MUSASHI, DEM SCHÖPFER VON FLASHTRO

Beachten Sie, dass das Interview, um im Saft zu bleiben, in der alten Schule
stattfand: Senden aller Fragen und Empfangen aller Antworten in einer guten 
alten TXT-Datei und vollständige Veröffentlichung. Der Text wurde aus dem
Englischen übersetzt.
Wie ist Ihr Interesse an der Recodierung von Cracktros in HTML5 entstanden?
Ich wollte Cracktros sehen, und die einzige Möglichkeit, dies auf dem PC zu 
tun, war die Verwendung eines Emulators. Da YouTube noch nicht existierte, war
die einzige Möglichkeit, sie anzusehen, sie selbst zu wiederholen. Ich habe
schon in den 90er Jahren mit Flash angefangen, damals ging es noch nicht ums
Programmieren. Es war nur das Zeichnen oder Erstellen eines Screenshots und 
das Animieren mit den Tools in Flash.
Ich konzentrierte mich zuerst auf meine Lieblings-Cracktros, die von Melon
Design für Crystal hergestellt wurden. Tatsächlich war meine erste Website ganz
Melon gewidmet. Nachdem ich alle meine Lieblings-Melon-Intros erstellt hatte,
ging ich nach und nach zu anderen einfach durchzuführenden (innerhalb
der Grenzen von Flash) Intros über.

Cracktro von Melon Dezign für Moonstone Crack von Crystal (Amiga 500)

Damals im Jahr 2004, gerade als YouTube aufkam, hatte ich gerade meine eigene
Website www.flashtro.com gestartet und erwartete nicht, dass sie sehr lange
bestehen würde, weil ich dachte, YouTube würde jeden, der eine Leidenschaft für
Cracktros hat, dazu bringen, damit aufzuhören einen Blick auf meine Seite zu
werfen. Warum sollten sie schließlich dorthin gehen, wenn sie sich ganze Videos
von Cracktros ansehen könnten?

Wie hast du die Cracktros ausgewählt?
Also begann ich mit meinen Favoriten wie den Melon/Crystal-Intros und ging dann
zu den Klassikern wie Paradox's Fucktro oder dem Sinus-Scroll-Intro von Angels
für Beast II über. Ich versuchte und schaffte es, Cracktros nur mit Text zu
vermeiden, da sie mich und andere ärgerten. Ich mache sie nur, wenn ich darum
gebeten werde oder wenn sie coole Musik zum Anhören haben.

Cracktro für Super Off Road Crack von Paradox (Amiga 500)

Als ich anfing, habe ich alles gemacht, was einfach aussah und einfach in Flash
zu replizieren war. Seitdem habe ich viel gelernt und interessiere mich oft
dafür, Cracktros herauszufordern, was mich dazu drängt, herauszufinden, wie sie
hergestellt werden und wie man sie am besten nachbildet.

Mit welcher Technologie rekodieren Sie die Cracktros?
Winuae ist das Muss! Wir können nichts ohne ihn tun (ein großes Dankeschön an
Toni Wilen). Danach habe ich kleine Grafikripper wie GFXRIP (alt und fehlerhaft
:P) oder Maptapper von Codetapper verwendet, um die Schriftarten oder Sprites 
zu erhalten, die ich möglicherweise benötige. Winuae ist großartig darin, Musik
von vielen Cracktros zu rippen, aber nicht so gut darin, exotische/proprietäre
Formate zu rippen.
Eine großartige Website, die ich benutze, um herauszufinden, welcher Track von
welchem ​​Cracktro stammt, ist Kestra Bitworld. Fast alle Cracktros sind hier zu
finden und bieten Ihnen großartige Informationen über sie, z.B. welches Lied 
verwendet wird, in welchem ​​Format es vorliegt usw.
Als ich Flash verwendet habe (nicht lange), habe ich Flash Pro CC und viele
frühere Versionen verwendet. Jetzt verwende ich nur noch einen Texteditor wie
Ultraedit, um das gesamte HTML5/JS einzugeben.
Ich verwende FLOD (http://www.neoartcr.com/), um die Musik wiederzugeben, oder
Web_UADE oder WEBSC68, wenn es sich um ein exotisches Format handelt, das FLOD 
noch nicht unterstützt. 

Wie rekodiert man einen Cracktro? Verwenden Sie seine Quelle?
Wenn die Quelle verfügbar ist, ist das eine große Hilfe, aber oft ist sie es
nicht. Ich muss dann mit einem Amiga Action Replay herumschnüffeln und
Haltepunkte im Code platzieren, um herauszufinden, was was tut. Wenn ich auf
eine Routine wie einen Sinus-Scroller stoße, schnüffele ich, bis ich die
verwendete Sinustabelle finde, und rippe die Tabelle, um sie in mein Remake
einzufügen.
3D-Zeug in Cracktros ist schwer zu replizieren, da sie nicht immer die gleichen
Code-/Sinustabellen für die Bewegung verwenden. Bei Bedarf habe ich zufällig
den 68000-Code für die gesamte 3D-Routine in JavaScript übersetzt (siehe das
Remake von Yragaels Cracktro für Paradox :P). Allerdings kostet es viel Zeit
und ich mache es nur, wenn es wirklich nötig ist.
Ich denke, das Fett in Cracktros kann repliziert werden, indem man einfach 
beobachtet, was passiert. Ein einfacher Scroller ist kein Problem. Statischer 
Text ist in 90% der Fälle supereinfach. Die restlichen 10% werden verwendet,
wenn Code verwendet wird, um den Text auf dem Bildschirm zu zentrieren, sodass
Sie den Code replizieren müssen. Sternenfelder sind einfach zu replizieren.
Alles, was Sie brauchen, sind Sternfarben, Sternmenge, Anzahl der Schichten / 
Geschwindigkeiten, und Sie sind fertig.

Wie lange dauert es, einen Cracktro umzucodieren?
Es kommt auf den Cracktro an. Ein einfacher Textbildschirm mit einem normalen 
Scroller und Musik kann weniger als fünf Minuten dauern. Ein komplexerer 
Cracktro wie der von Paradox für die Celica GT Rally kann ein paar Tage dauern,
da ich noch nie einen solchen Effekt gesehen oder erzielt habe. Unter diesen 
Bedingungen musste ich ins Detail des Intros gehen, um herauszufinden, wie der
Scroller funktioniert. Außerdem musste ich verstehen, wie der Übergang zwischen
einem Scroller und dem nächsten stattfindet (Wartezeit, was den Übergang 
auslöst usw.)

Cracktro für Celica GT Rally Crack von Paradox (Amiga 500)

Ist es Ihnen jemals schwergefallen, einen bestimmten Cracktro neu zu codieren?
Nicht wirklich. 96% der Cracktros verwenden ähnliche Routinen wie Sinus-
Scroller, statische Textbildschirme, Copperhintergründe usw. Wenn wir einige
Cracktros umcodieren, können wir den Code für die folgenden wiederverwenden.
Wenn es jedoch um das 3D-Zeug geht, sind die Drehungen, obwohl es einfach zu
replizieren ist, sobald Sie die 3D-Punktdaten haben, schwierig, 100% der Zeit
zu replizieren, da jede 3D-Routine anders ist: Sie verwendet 
Berechnungsmethoden andere Rotation, andere Sinus-/Cosinuszahlen usw.

Wurden Sie jemals von Code-Tricks in einem Cracktro überrascht?
Nein, da ich kein "REAL CODER" auf Amiga bin (ich habe gerade genug gelernt, um
Cracktros / Game Cracking zu studieren).
Ich bin jedoch überrascht, wie viele Cracktro-Autoren versuchen, ihre Intros so
gut wie möglich zu schützen. Manchmal stoße ich auf ein Intro, das ich
replizieren möchte, und ich kann den von den Scrollern verwendeten Text nicht
im Speicher finden.
Ich schaue mir den Code genauer an und stelle fest, dass der Cracktro-Autor den
Text mit einem OR.B mit einer Zufallszahl verschleiert hat. Wenn das Intro
läuft, zeigt es auf etwas, das im Speicher wie Kauderwelsch aussieht, aber das
ist, um es mit einem OR.B mit einer Zahl zu kombinieren und so das richtige 
Zeichen für den Scroller zu erzeugen. Ich denke, sie haben das gemacht, um 
Hacker daran zu hindern, ihr Intro zu stehlen und es für ihre beschissene 
Gruppe auf der Bühne zu verwenden (was oft passiert ist).
Ich war wirklich schockiert, als sich herausstellte, dass ein alter Cracktro
aus Skid Row mit demselben Schutz codiert war wie eine alte Copylock-Routine
von Rob Northen. Der Cracker/Autor des Cracktro wollte zeigen, dass er Robs
Schutz nicht nur knacken, sondern sogar seine eigenen Intros kodieren kann.

Was magst du an Cracktros?
Keine Ahnung :P. Ich weiß nur, dass ich mir manchmal ein neues Spiel geschnappt
habe, als ich meinen Amiga hatte, nur um zu sehen, ob es einen neuen Cracktro
hatte :D.
Ich glaube, ich bin fasziniert von der Cracking-Szene. Wenn ich die Nachrichten
in den Cracktros lese und sehe, dass die gleichen Namen in den Begrüßungen 
wiederkommen, frage ich mich, wie es funktioniert hat. Wie konnte ein Typ in
Dänemark ein Spiel knacken und ich, der ich in Großbritannien lebte, konnte es
nach ein paar Tagen spielen?
Außerdem halte ich es für eine Kunst. Jedes wurde von einem Künstler gemacht, 
der sich eines Tages mit einer Idee dessen, was er wollte, hinsetzte und sie
für Tausende von Menschen auf die Leinwand brachte.

Ein Lieblingscracktro?
Es gibt so viele, aus verschiedenen Gründen.
Super Offroad von Paradox (komm schon!)
Celica GT RALly von Paradox (außergewöhnlicher Scroller!)
Prince of Persia von Angels (was für Musik und was für ein Sinus-Scroller!)
Mondstein von Crystal/Melon (tolle Musik und toller Effekt auf dem Logo!)
Vroom von Paradox (diese Würfel, die außergewöhnliche Musik von Monty!)
Vroom von Vision Factory (schöner Scroller und fabelhafte Musik!)
4D Sports Driving of Crystal/Melon (toller Punkteffekt!)
Cracktro für Prince of Persia Crack von Angels (Amiga 500)

Hatten Sie Kontakt zu Autoren von Cracktros?
Im Laufe der Jahre stand ich in Kontakt mit den Jungs von Melon (Paleface,
Seen, Bannasoft), Rog (Vision Factory), Brainwalker, Vision One (AFL) und
zuletzt Yragael.

Haben sie dir Dinge beigebracht?
Nicht viel. Es ging im Grunde darum, mir eine Nachricht zu schicken, in der ich
mir für die Konvertierung bedankte, oder kleine Geschichten darüber, was sie
dazu inspirierte, an einem bestimmten Cracktro zu arbeiten.

Was sind deine Pläne für Flashtro?
Machen Sie einfach so lange Remakes, bis keine Besucher mehr auftauchen.
Ein letztes Wort, bevor Sie berühmt werden (inklusive Grüßen)?
Begrüßen Sie einfach alle auf flashto.com und danken Sie ihnen für ihre
Unterstützung in den letzten dreizehn Jahren.
