EIN ASPEKT DER AMIGA-SZENE

Der Amiga war Commodores letzte Reihe von Verbrauchermaschinen. Seine
Vermarktung war ein Misserfolg in den Vereinigten Staaten, aber ein Erfolg in
Europa. Der 1987, zwei Jahre nach dem Amiga 1000, herausgebrachte Amiga 500,
der große Rivale des Atari 520 STF, ließ auf dem alten Kontinent eine
internationale Gemeinschaft von Enthusiasten entstehen. Unter den letzteren
bildeten einige eine eigene Band, die unter das fiel, was man nennen sollte
- und immer noch so nennen sollte, weil es glücklicherweise so bleibt - 
die Bühne.

Commodores Amiga 500, veröffentlicht 1985

Die Szene war vor allem in Gruppierungen strukturiert, die von legal über
illegal bis hin zu regelrecht mafia reichten. Ende der 80er taten sich dort
Gruppen von Demomakern zusammen , die sich aus Programmierern zusammensetzten,
die mit Grafikdesignern und Musikern in Verbindung standen, um Demonstrationen
von Know-how zum Nachdenken zu produzieren - Demos, Intros und andere
Cracktros - und Gruppen, die die Früchte ihrer Hacks vermarkteten, bestehend
aus Leadern , Lieferanten , Crackern und anderen Tauschern. Hinzu kommen
Bulletins für Sysops, die Board-Systeme verwalten (BBS), dh über Modems
zugänglich gemachte Fileserver auf Amiga, und die Bestandsaufnahme der Akteure
der Szene wird nahezu vollständig sein. Oh ja! Ich wollte das Wesentliche
vergessen, nämlich den Grundnutzer, der oft karikaturistisch in der Person
des Lahmen dargestellt wird. Wir werden darauf zurückkommen.
Von alledem wären nur glückliche Erinnerungen in der Erinnerung der Jungen -
und weniger Jungen - vergangener Zeiten geblieben, wenn sich nicht jemals
einige damals, ohne es zu wissen, vorgenommen hätten, die Chronisten ihrer Zeit
zu werden, indem sie über den Moment berichteten, was sie dachten seien die
Neuigkeiten. Es ist das Fanzine auf Diskette, das Diskmag, das damals als
ihr Medium dienen sollte.

Er hatte viele Diskmagazine : Chit Chat , Cracker Journal , DISC , Deadlock ,
Fourth Dimension , Grapevine , Hack-Mag , Maggy , McDisk , Outer Limits ,
PENIS , RAW , Rage ! , Szenentext , Szenengespräch , Gestohlene Daten ,
Selbstmord , Streng geheim , Handelszentrum , Upstream , Visuelle Intensität,
Zine , um nur einige zu nennen. Die Titel scheinen zu suggerieren, was sie
verbergen, saftige Informationen darüber, was im Inneren der Szene passieren
könnte. Wenn man sich jedoch die Menüs anschaut, in denen die Elemente
aufgelistet sind, wird deutlich, wie oberflächlich es wäre zu glauben,
man sei auf mikrokosmische Versionen von Gala gestoßen.

