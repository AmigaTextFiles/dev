sind die stärksten und schwächsten Fußballmannschaften im Moment?
Die Beteiligung der Autoren am Schreiben von Artikeln vonSonstiges ist sehr
unterschiedlich. Offensichtlich wird von ihnen nicht erwartet, dass sie ihre
eigene Reflexion produzieren, sondern etwas Interessantes berichten, auch wenn
sich die Arbeit darauf beschränkt, eine anderswo veröffentlichte Schrift
fortzusetzen. Der Leser geht daher sofort von einer einfachen Chronik
verschiedener umformulierter Ereignisse - in "Fighting Talk" übernimmt eine
anonyme Person von GREEN eine Liste von "Exploits" von Umweltaktivisten - zu
einem sehr persönlichen Bericht - in "Going To A Cremation " schildert
mUb / LSD seine erste Erfahrung mit einer Einäscherung, die er in einem
lächerlichen Licht darstellt. Es sind die Artikel dieses letzten Registers, die 
am reichsten sind. wo Fish / LSD auf nicht weniger als 14 Seiten von seinen
Erfahrungen als behinderter Mensch berichtet, der wegen Multipler Sklerose
drei Jahre lang an einen Rollstuhl geschraubt war. "Ich hatte Freunde, 
während ich spazieren ging, aber sobald ich in den Stuhl ging, wollten meine
