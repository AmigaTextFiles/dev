EINE FÜLLE VON INFORMATIONEN ÜBER DIE SZENE

Also, was steht auf der Speisekarte von Grapevine #8? Die im Inhaltsverzeichnis
aufgeführten Inhalte sind besonders vielseitig.
Erstens der redaktionelle Klassiker, der in diesem Fall die Existenz eines
Passwortsystems erwähnt, das den Zugang zu einem geheimen Artikel ermöglicht -
ein Markenzeichen der Grapevine -Redaktion, wie es scheint. In diesem Fall wird
es "rekodiert", gefolgt von Enter nach dem Drücken von ESC, was den Zugriff auf
eine Liste von Nachrichten von Mitgliedern von LSD an andere Mitglieder der
Szene ermöglicht.
Beispielsweise gratulieren Watchman / LSD - in der Szene identifizieren wir uns
durch ein Paar [Pseudonym] / [Gruppe] - Monty Python / LSD für die Qualität
seines Codes: "How come you can code is so fucking good. Are you ein
Außerirdischer oder so?" Verständlicher als die Nachricht von Torch/LSD an
Marc Panther/Soudwave: "Oh, mach weiter. Sag es mir. Ich werde dein bester
Freund fürs Leben sein. Ehrlich!"
Hier ist nichts zu geheim. Dies sind Grüße, eine Art der Kommunikation durch
öffentliche Nachrichten, deren Zweck es ist, zum Ansehen des Empfängers
beizutragen und gleichzeitig zum Ansehen des Autors beizutragen, indem sein
Beziehungsnetz angezeigt wird. Keine Bühneninszenierung, die keine Grüße
enthält ; es wäre wie die Veröffentlichung eines wissenschaftlichen Artikels
ohne Angabe einer Bibliographie gewesen - manchmal nicht immer verwendet!
Namedropping , heißt es, der Inhalt der Nachrichten falle oft in den privaten
Scherz und sei daher für Dritte völlig undurchsichtig, bis hin zur
Begrüßungwurde auf eine Liste von [Pseudonym] / [Gruppe] beschränkt? 
Zweifellos, aber nicht nur, denn wenn ein Thema in den Äußerungen derjenigen
auftaucht, die die Szene in den Diskmags beschreiben, dann ist es vor allem
das der Freundschaft.
Lassen Sie uns die Seiten umblättern oder scrollen, um genauer zu sein. Nach
einer Hilfe, hier ist ein Teil, der sich an die Mitwirkenden richtet. Offenbar
sind alle Artikel willkommen: "Hinweise und Tipps, Meinungen, Rezensionen,
Witze, alberne oder ernste Geschichten, Zeichentrickfilme und so ziemlich alles
andere, was Ihnen einfällt!" Es ist wahrscheinlich weniger der Offenheit als
der Füllung wegen, dass diese redaktionelle Zeile angezeigt wird: Es ist
notwendig, Inhalt zu finden, um die Zahlen zu füttern, die gegen Ende des Amiga
manchmal bis zu zwei 880-KB-Disketten mit komprimierten Daten umfassen werden
- einschließlich Bilder, immer umfangreich, es ist wahr.
Anweisungen werden für potenzielle Mitwirkende bereitgestellt. Die Ära ist noch
nicht HTML, und jedes Diskmag verlässt sich auf ein bestimmtes System, um eine
Textpräsentation zu verwalten, die immer zusammenfassend, aber effektiv bleibt.
Bei Grapevine ist es möglich, die Farbe des Textes festzulegen, seine Dicke zu
halbieren, seine Höhe zu verdoppeln und ihn blinken zu lassen, indem ihm
bestimmte Zeichenfolgen vorangestellt werden. Daher zeigt
{2{9{8{7Hello " Hello" mit Zeichen mit einer Dicke von einem Pixel, in Rot,
doppelter Höhe, blinkend an.
Der nächste Teil ist dem Abonnement gewidmet. Bei Versand per Post zu 8 Pfund
