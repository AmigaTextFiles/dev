IM ÜBRIGEN EIN INHALT, DER REICH AN EKLEKTIZISMUS IST

Die folgenden Abschnitte über Humor und Quiz sind weniger interessant, um in
die Ära zurückzutauchen. Unter diesem Gesichtspunkt ist Diverses erbaulicher,
da es nicht nur einen Zugang zu Aspekten der Szene, sondern allgemeiner zur
damaligen Gesellschaft ermöglicht.
Durch die Beschreibung, wie man einen "extrem effektiven "Abhör"-Sender mit
einer maximalen Ausgangsleistung von 2 Watt optimiert! Abstimmbar von 88 auf
100 MHz, natürlich ..." , erinnert uns Maxima / LSD an die verschwundene Welt 
von CB. James 3/Pol?rone produziert einen sehr umfassenden Episodenführer zu 
Blake 7 , das als äußerst beliebte BBC-Science-Fiction-Serie in Rechnung
gestellt wird. MFD / The Silents diskutiert ein entscheidendes Thema 
