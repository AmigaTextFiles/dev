DER LAHME , SÜNDENBOCK DER SZENE

Sache versprochen, Sache fällig. Es wurde mehrfach erwähnt, und jetzt muss mehr
darüber gesagt werden. Wenn es einen Schauspieler gibt, der nicht auf der Party
in den Diskmags ist, dann ist es der Lahme.
Ab Nummer 6 veröffentlicht Stolen Data den "Lahmen Brief der Ausgabe", der es
ermöglicht, besser zu definieren, was diesem von Ausgrenzung betroffenen Akteur
der Szene vorgeworfen wird. Mole/Anarchy kommentiert eine Mail von
Phoenix/Timecode, die ihm zwei Disketten schickt, um mit ihm zu reden: eine
Liste dessen, was er hat, und eine sicherlich raubkopierte Version von Wings of
Fury. Was hat er nicht getan! "Zum ersten und zum letzten Mal BIN ICH KEINE
VERDAMMTE WOHLTÄTIGKEIT!", schreibt ein Maulwurf, der offensichtlich das Gefühl
hat, keine Zeit mit jemandem zu verschwenden, der vorgibt, auf sich selbst
zuzugreifen - auch wenn es offensichtlich so demütig wie höflich ist. Sichtlich
sehr irritiert verurteilten Mole/Anarchy Timecode mit dem Versprechen, sich in
zukünftigen Ausgaben von diskmag systematisch über die Gruppe lustig zu machen.
Der Lahme ist jemand, der gerne dabei wäre, aber durch seine Tollpatschigkeit -
oder, ganz erschwerend, durch seine List - beweist, dass er es nicht kann. Es
ist eine absolute Folie, und es gibt offensichtlich nichts Schlimmeres, als
beschuldigt zu werden, ein Lahmer zu sein , besonders im öffentlichen Forum,
das ein Diskmag ist. Jeder Bewerber ist ein potenzieller Lahmer, daher wird von
ihm erwartet, dass er sich zurückhält, dass er es nicht wagt, diejenigen zu
stören, auf deren Niveau er nicht aufgestiegen ist, und dass er vor allem nur
einen Heiligenschein der Verdienste trägt, die andere ihm verleihen angesichts
der Früchte seiner Arbeit und keine Unze mehr.
Wenn es nicht umgekehrt ist, war der Begriff "lamer" der Begriff " blade", um
eine Aktivität zu bezeichnen, die nicht von Interesse ist. In Stolen Data #8
kehren Chromag/Cult in "Cool Or Lame Swapping!" zurück. mit etwas Verbitterung
über seine intensive Praxis des Tauschens, die er im Nachhinein als lahm
beurteilt: "Was ich sagen will, ist, dass man sich vor XCopy aufhängt und nur
kopiert, kopiert, kopiert … ist nicht der Weg, um Freunde zu finden." Zum 
Beispiel sind wir nie vor uns selbst sicher.
Wir dürfen aber nicht beim Bild einer Szene bleiben, deren Mitglieder
leidenschaftlich gern den Sündenbock benennen würden. Um mit einer 
einvernehmlicheren Note zu enden, werden wir daher Franz / Italian Bad Boys
aus dem oben zitierten Interview zitieren: "Ich denke, das Beste, was der
Amiga mir gegeben hat, war, dass ich dank dieser Maschine viele Freunde um
mich herum habe Welt, Jungs, die ich nie gesehen habe und wahrscheinlich nie
sehen werde, und wir sind wirklich Freunde, als Jungs. Das ist eine
wunderbare Sache!"

ZU ENDE ... UND FÜR IMMER

Die Veröffentlichung von Diskmags - höchstens monatlichen - hat aus
verschiedenen und unterschiedlichen Gründen nicht immer eine strenge
Periodizität eingehalten, von denen einige sicherlich jedem bekannt
vorkommen werden, der jemals in der Redaktion eines sehr klassischen Magazins
gearbeitet hat. So entschuldigt sich Oedipus / LSD in seinem Beitrag zum 
Leitartikel von Grapevine #21 , der aus Zeitmangel von #9 vierzehntägig wurde,
für die Verzögerung, indem er erklärt, dass die Festplatte von Pazza / LSD
abgestürzt ist, was zum Verlust vieler Artikel geführt habe. Er erklärt auch,
dass sich die Organisation des Teams geändert habe, sagte Pazza, der seine
Rolle als Chefredakteur aufgegeben habe, um Ödipus zu übergeben.
Die Lebensdauer von Diskmags muss an derjenigen der Amiga-Szene gemessen
werden, die sicherlich nur wenige Jahre gedauert haben wird. 1987 erschien der
Amiga 500, Commodore ging 1994 aus dem Geschäft. Zwei Jahre zuvor hatte der
Hersteller versucht, der PC-Welle mit einer Erneuerung seines Sortiments
entgegenzuwirken, insbesondere mit dem Amiga 1200, doch der Versuch war
gescheitert. Die Amiga-Szene überlebt nicht, zumindest nicht in der uns
bekannten Form. Ein paar Jahre sind kurz, aber man muss nur Red Sector Inc.s
Megademo von 1989 und How 2 Skin A Cat von Melon Dezign von 1993 vergleichen,
um zu verstehen, dass die Szene in kürzester Zeit ihre Codes revolutionieren
konnte.
Eine Liste von Diskmagazinen legt nahe, dass sich das Genre erst gegen Ende der
80er, oder besser gesagt Anfang der 90er Jahre auf dem Amiga entwickelt hat.
Die letzten Nummern vieler von ihnen gehen auf das Ende von Commodore zurück.
Grapevine Nr. 21 wurde 1995 veröffentlicht, Top Secret Nr. 16 1994,
RAW Nr. 9 1995 usw. Der Untergang des Amiga trug zwangsläufig zum Absterben
von Diskmags bei, aber auch andere Faktoren sollten nicht unterschätzt werden.
Tatsächlich haben die zitierten Diskmags eine außergewöhnliche Langlebigkeit
gezeigt, denn nur wenige haben zwanzig Ausgaben überschritten. Wieso den?
Auch hier liefert das Lesen der Artikel Informationen darüber, was passiert
sein könnte. In Grapevine #21 verabschiedet sich Pazza/LSD auf drei Seiten.
"Jetzt habe ich einen Job, eine Frau und ein Haus (ich bin aus einer Wohnung
gezogen). , er erklärt. Leben ist Leben, und es spielt sich nicht nur auf dem
Bildschirm ab. Oder noch einmal, in Stolen Data #9 legt RokDaZone auf vier
Seiten seinen Unmut darüber dar, dass die Diskmag-Redaktion so wenig für ihre
Arbeit belohnt wird. "Also, warum werden Redakteure immer noch als Typen
zweiter Klasse angesehen?" , rebelliert er, nachdem er sich daran erinnert hat,
dass ein Diskmag ohne Inhalt kann nicht von Interesse sein. Er kündigt nicht
an, für all das aufzugeben, aber es geht ihm entschieden sehr schlecht.
Zweifellos hätte der Mangel an Anerkennung andere entmutigen können. In
Chit Chat Nr. 14 unterschreibt r@T@p einen drohenden Leitartikel, in dem
er die Leser dafür kritisiert, dass sie nicht genug beitragen: "Wenn die
Unterstützung für Ausgabe 15+ auf dieser speziellen Ausgabe läuft, werden
wir definitiv aufhören, Chit Chat zu machen!" Drohung übrigens ausgeführt,
Chit Chat hat bei der nächsten Nummer aufgehört...
Vielleicht blieben einige Diskmags eine Zeit lang im Netz, wie RAW , dessen
Nummer 9 mit mehr als 300 Zugriffen pro Tag stolz den Erfolg von RAW online
verkündete. Die Site war unter einer URL zugänglich, die heute das Gütesiegel
von Vintage angenommen hat, weil sie auf ein Benutzerverzeichnis verweist,
http://www.xs4all.com/~blahh ,
aber sie ist natürlich längst verschwunden und hat weniger von einem
hinterlassen Spur als die Diskettenversion - genug, um sich allgemeiner um die
Erhaltung aller online veröffentlichten Inhalte zu kümmern.
Ein Vierteljahrhundert später ist die Frucht der mühevollen Arbeit der
Herausgeberinnen und Herausgeber glücklicherweise erhalten geblieben, und die
bloße oberflächliche Lektüre einer früher unternommenen Ausgabe eines Diskmags
will den Reichtum des Materials demonstrieren, das dieses Werk für die
Rekonstruktion darstellt eine vergangene Zeit. Jeder, der sich für Diskmags
interessiert, findet mit den Schlüsselwörtern "Amiga" und "diskmag" leicht
eine Menge davon auf mehr als einer Website, beginnend mit der Kestra- Site,
ganz einfach. Es wird gemunkelt, dass die Site http://grandis.nu/
Zugriff auf eine FTP-Site bietet, deren Verzeichnis Commodore_Amiga würde ein
TOSEC- Verzeichnis (kurz The Old School Emulation Center) enthalten, das ein
gigantisches Commodore Amiga-Diskmags-Verzeichnis enthalten würde, das
Äquivalent der Bibliothek von Alexandria für die hier diskutierten digitalen
Kohlblätter. Ruhig! Ich habe es dir nicht gesagt...
In einer schönen Rezension des Films Les Invasions Barbares kehrt Gérard Allard
zu dieser faszinierenden Passage zurück, in der die Kamera auf einigen der
Werke verweilt, die in der Bibliothek eines verschwundenen Intellektuellen
thronen. In Anlehnung an das Tagebuch von Samuel Pepys schreibt der Autor:
"Dieser Mann, der nicht viel war, erzählte seine Zeit, ohne es zu wissen, indem
er sich ganz dumm sagte. "Ich möchte diesen schön formulierten Satz ausleihen,
um zu sagen, was die Autorinnen und Autoren der teilweise sehr persönlichen
Texte, die ich erwähnt habe, heute beitragen können. Auch wenn das Mitwirken
an Disketten-Zines damals oberflächlich erschien, scheint es im Nachhinein
nicht so gewesen zu sein. Was fuer eine Ueberraschung? Es gibt nie etwas
Unnützes in dem, was du tust, solange es kreativ ist, auch wenn es nicht auf
die Zeit ankommt.

