Wer wirbt in Diskmags? Gruppen, die expandieren möchten, indem sie einen
Musiker, Grafikdesigner oder Programmierer rekrutieren. Andere, die versuchen,
Menschen zu ihrem BBS zu locken - Zugang, den sie verkaufen können, um
Raubkopien an diejenigen herunterzuladen, die von Beiträgen abgestoßen werden
- ein Behindertenanteil an einem großen deutschen BBS, was könnte man mehr
träumen? Privatpersonen, die tauschen wollen, verstehen darunter
Austauschdisketten per Post, die Software enthalten, die hier noch oft
raubkopiert wird, aber nicht nur. Und auch Gruppen, die ausschließlich illegal
handeln, Abonnements vermarkten, um die neuesten Spiele per Post zu erhalten,
sobald sie veröffentlicht werden, Maschinen zum Simulieren von Super
Nintendo-Spielkassetten usw.
Aber der Inhalt, der Inhalt! Hier ist es, unterteilt in mehrere Bereiche:
News & Events , Gruppeninformationen , Interviews , Computerinformationen, 
Informationen zur Amiga-Szene , Humor , Quizzes , Verschiedenes. Und wenn das
nicht genug wäre, lassen Sie uns wissen, dass wir in der letzten Grapevine #21,
die nicht weniger als 200 Artikel enthält, so weit gehen werden, zu finden:
Allgemeine Computer , Computerspiele , Hacking und Telekommunikation , Coder's
Corner , Amazing But Wahr , seltsam, Drogen (die sich mit dem Thema unten
befassen, wie "Sollte Cannabis legalisiert werden?" ), Belletristik , Filme ,
Fernsehen und andere Medien , Musik , Fantasy-Rollenspiele , Poesie , 
Vegetarismus und Tiere. Die Überschriften sind manchmal ad hoc, abhängig
von den Artikeln, die die Redaktionen erreichen.
Unter den wesentlichen Abschnitten ist News & Events zu erwähnen , wo die
Redaktion von Grapevine #8 die neusten Informationen aus dem Szeneleben
zusammengetragen. Insbesondere gibt es Artikel, die zukünftige Spiele
ankündigen, die sich unvermeidlich auf Demo-Wettbewerbe konzentrieren, bei
denen Gruppen aus der ganzen Welt eingeladen werden, die Talente ihrer
Programmierer, Grafikdesigner und Musiker für ein paar Tage zu zeigen.
Protokolle von Spielen, die ebenfalls abgehalten wurden. Nun, hier ist einer,
der auf Initiative der IRIS-Gruppe in Nesles-la-Vallée abgehalten wurde. Es
hätte mehr als 400 Leute versammelt - der Eintritt war jedoch nicht gegeben:
60 Franken, oder 40 Franken für den, der mit ... seinem Computer kam. Hey, der
Amiga 500 war kein Laptop! Gehen Sie damit spazieren! Beachten Sie, dass
Shadow / LSD, der die Feder in der Hand hält, unter anderem eine interessante
Tatsache berichtet, nämlich dass die Party von der gesponsert wurde "Wie Sie
wissen, wurden nur legale Sachen, Tools und Programme akzeptiert", präzisiert
er, was nicht verwunderlich ist, wenn wir uns an die Verbindungen zwischen
Titus und der Program Protection Agency (APP) erinnern.
Group Information fasst Gruppenpräsentationen zusammen. Bei Artikeln, die von
Mitgliedern der betreffenden Gruppen geschrieben wurden, ist der Ton
offensichtlich hagiographisch, aber das tut dem Interesse keinen Abbruch. Im
Gegenteil, es ist sehr aufschlussreich zu beachten, was die Autoren für
wesentlich halten, was also eine Gruppe in ihren Augen ausmacht. So zeigt die
Gruppe End Of Century 1999, dass eine Gruppe eine kollektive Identität ist, die
es zu verteidigen gilt: "Wir haben uns entschieden, diese aufzunehmen, weil 
bestimmte Leute behaupten, im EOC 1999 zu sein, und doch waren sie "nicht",
also ist hier ein Vollmitglied aufführen". Und der Aufruf an die Programmierer,
der der Ankündigung der laufenden Produktionen folgt, erinnert uns daran, dass
eine Gruppe nur insoweit existiert, als sie produziert, so sehr, dass es für
sie unerlässlich ist, zu rekrutieren. Talente zu finden scheint nicht einfach
zu sein. So präsentiert Loco/Paragon seine Gruppe als Ergebnis der Fusion von
Bohemians und Artic Force aufgrund von Mangel: "Der Grund, warum wir eine
völlig neue Gruppe gegründet haben, war, dass Artic Force einen schrecklichen
Bedarf an Grafikern hatte." Allerdings ist die Notwendigkeit, einer Band
beizutreten, um in der Szene bestehen zu können, nicht zu unterschätzen - wer
gleichzeitig Grafikdesigner, Musiker und Programmierer sein kann, ist extrem
selten. Andy / Doom - übrigens "Wir brauchen dringend einen Musiker!" -
Erklären "TKI schloss sich uns an, da er eine Gruppe brauchte, der er beitreten
konnte, und T2 auch" und wieder, dass "Deathlok/Eclipse (ehemals Killers)
Killers auf der Suche nach einer neuen Gruppe verließ, der er beitreten 
konnte." Übrigens ist anzumerken, dass die Szene instabil ist: Die Gruppen
kommen und gehen in der Szene, genauso wie die Mitglieder in den Gruppen kommen
und gehen.
Dies ist nur ein kleiner Teil der Informationen, die aus dem Lesen der Artikel
in diesem Abschnitt gezogen werden können. Aber als ob das nicht genug wäre,
trägt der Abschnitt Interviews noch mehr dazu bei, uns über die Soziologie der
Szene zu erbauen, durch die Erzählung, die diejenigen, die sie bilden, nicht
nur von ihren Gruppen, sondern auch von sich selbst machen. Es stellt sich
heraus, dass die Altersgruppen am unterschiedlichsten sind, wobei man davon
ausgehen muss, dass der Durchschnitt bei etwa 20 liegen muss: Clauz / Extreme
ist 17, Pride / Brainstorm ist 20, Numitor / Pulsar ist 26 und Franz / Italian
Bad Boys hat 40! Beide werden insbesondere nach ihrem Profil, ihren Hobbies,
ihrer Vision von der nationalen und internationalen Szene befragt - auf ihr 
Land fallen die Worte des jeweils anderen mitunter grausam: "(lame?)" fragt der
Franzose Clauz / Extreme; "ein bisschen lahm" sagt Danish Krueger / Kefrens 
-, ihre persönliche Rangliste ihrer verdientesten Bands und Mitglieder, ihrer
Band und ihrer Projekte.
In Computerinformationen behandeln die Artikel die unterschiedlichsten Themen
rund um die Technik. In "Coder's Corner", Orcrist / LSD erklärt - ziemlich
prägnant, aber immerhin - wie man 3D in Echtzeit programmiert, indem man sich
insbesondere auf den Blitter stützt, einen der grafischen Coprozessoren des
Amiga. Weiterhin testet Ken D. den Amiga 500+, eine neue Version des Amiga 500,
ausgestattet mit leicht verbesserten Koprozessoren, aber vor allem mit einem
neuen Betriebssystem im ROM: Kickstart 2.0 ersetzt Kickstart 1.3. Im 
folgenden Artikel liefert Darren Ewaniuk eine sinnvolle Ergänzung, indem er
erklärt, wie man einen Switch installiert, um zwischen den beiden ROMs
umzuschalten und so die Abwärtskompatibilitätsprobleme zu lösen. In einem
völlig anderen Register, das für Hacking viel obskurer ist, meldet Shadowrex
die von AT& bereitgestellten Anweisungender Abbau eines ganzen Netzes Ende
1994. 
Um auf dem Laufenden zu bleiben, fügen wir abschließend hinzu, dass eine
anonyme Person auf weniger als neun Seiten eine Bestandsaufnahme der
verschiedenen Game Doctors auf dem Markt erstellt - "'Game Doctor' ist der
Namensgeber für Geräte, die das ROM auf dem lesen können Videospiel-Cartridges
und speichere sie auf Diskette" , für diejenigen, die es immer noch nicht
wissen.
Der Abschnitt Amiga Scene Information ist fast verschieden, da der Inhalt
vielseitig ist. Alle Artikel konzentrieren sich jedoch auf die Szene und haben
zum Ziel, die Praktiken zu kommentieren oder auf pädagogische Weise
aufzudecken. Als solches gibt ihre Lektüre eine Vorstellung von den täglichen
Problemen, mit denen Amiga-Besitzer konfrontiert sind, die auf die eine oder
andere Weise in der Szene involviert sind.
Und zuerst, wie füttert man das gefräßige Diskettenlaufwerk der Bestie? In
einem spannenden "A Swapper Guide" von nicht weniger als sieben Seiten
erklären Phreak / Jesters insbesondere, wie man Briefmarkenfälschungen
praktiziert, um die Versandkosten zu begrenzen und so Disketten per Post
einfacher auszutauschen. Dies wird nicht dadurch erreicht, dass der Stempel mit
Harz überzogen wird, erklärt er, bevor er seine fünfstufige Methode zum Drucken
gefälschter Stempel entfaltet, die er für unaufhaltsam hält - "Fools them 
everytime" .
Wie man die Royal Post laut Phreak/Jesters in Grapevine #8 täuscht
Aber schließlich "Piraterie - ist es wirklich ein Verbrechen?" Auf zwei Seiten
erläutert mUB / LSD die anstehende Frage, die Spielehersteller anzuprangern,
die auf dem Rücken der Entwickler Geld verdienen und die lügen, indem sie
behaupten, dass ihre Software weniger raubkopiert wäre, wenn sie weniger teuer
wäre. Er schließt mit einem Urteil über Solomon, vor dessen Logik ein
marxistischer Ökonom zweifellos nicht zurückweichen würde: "Halten Sie das
Piraterie-Balance-Niveau bei Ihrer Suche nach Raubkopien von Software (obwohl
es unmoralisch ist usw.) fort, aber seien Sie nicht zu gierig (lasst den
Entwickler leben, aber erwürgt den Kapitalisten)".
Andere Themen werden angesprochen, die weniger Lahme betreffen. Das Rippen, 
das heißt der Diebstahl von Code, Grafiken, Musik und sogar Artikeln zur
Wiederverwendung in anderen Produktionen durch skrupellose Personen, die so
weit gehen können, sie sich anzueignen, ist eine Quelle, die immer wieder
Anlass zur Sorge gibt. Glücklicherweise bietet ein Diskmag eine Plattform, um
gerecht zu werden. Auch Steinar/Addonic nutzt die Gelegenheit zum Jammern auf
den drei Seiten von "Ripping Is Not The Only Sin" dass Quartz eine 
Musikdiskette herausgebracht hat - eine Zusammenstellung von Musik - wo die
Band, ohne um Erlaubnis zu fragen, eine ihrer Kompositionen übernahm, indem sie
ihren Titel kratzte, vergaß, ihn zuzuordnen, und die Kühnheit dazu brachte, ein
Instrument zu modifizieren - "Das ist das Juwel in der Krone!" Wir verstehen,
dass es in einer Umgebung, in der die symbolische Hierarchie auf persönlichen
Verdiensten basierte, nicht unbedeutend war, des Rippens beschuldigt zu werden.
Denn wie Conquest / Anarchy in "lamers...Get Lost!" veröffentlicht in Stolen
Data #10 ,"Es gibt zwei Arten von Szene: Die 'gute', auch als Elite bekannt,
und die schlechte, die aus Typen besteht, die von der Elite 'lamers' genannt
werden! Die erste arbeitet sehr hart daran, neue Standards auf dem Amiga zu
setzen ... sie produzieren nur hochwertige Sachen und wollen Sachen von
gleicher Qualität zurück sehen, während die Lamers alles zerreißen, um ein paar
Credits zu bekommen und sich zu rühmen!" Auf die Figur des Lahmen werden wir
später noch einmal zurückkommen, da Reißen nicht das einzige Verbrechen der
Majestätsbeleidigung ist, das er begehen kann.
Beachten Sie, dass sich in der "Rogues Gallery" der Amiga Scene Information-
