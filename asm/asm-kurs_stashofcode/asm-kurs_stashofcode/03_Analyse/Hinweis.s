
Hinweis zur deutschen Übersetzung der Stash Of Code Serie zum Amiga.

- Im Ordner Lektionen sind die deutschen Übersetzungen der Artikel enthalten.
- Im Ordner Listings sind die Beispiellistings aus den Artikel mit deutschen
  Kommentaren enthalten. Die Programme entsprechen dabei den Originalen.
  Nur einige Variablennamen wurden etwas geändert.
- Im Ordner Analyse sind Unterordner zu den Kapiteln. In diesen befinden sich
  Programmbeispiele mit ausführlicheren deutschen Kommentaren. Um die Programme
  besser zu verstehen gibt es kleine Programmauszüge zum Debuggen.
  
   
