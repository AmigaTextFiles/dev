	BNE.B	LOOP
ENDE	MOVE.W	#%0111111111111111,$DFF096
	MOVE.W	#%0111111111111111,$DFF09A
	MOVE.W	#%0111111111111111,$DFF09C
	MOVE.L	OLDIRQ,$00006C
	MOVE.W	DMACON,D0			
	OR.W	#$8000,D0			
	MOVE.W	D0,$DFF096
	MOVE.W	ADKCON,D0			
	OR.W	#$8000,D0
	MOVE.W	D0,$DFF09E
	MOVE.W	INTENA,D0			
	OR.W	#$C000,D0
	MOVE.W	D0,$DFF09A
	CLR.W	$DFF088
	MOVEM.L	(A7)+,D0-A6
	RTS

** CREATE CIRKEL TABLE ********************************************************

ELEMENTS = 400
RADIUS = 87
STARTX = 87

INITCIR	MOVE.W	#STARTX,D1
	MOVE.W	#STARTX,D2
	LEA.L	TABLE,A0
	LEA.L	TABLE,A1
	ADD.L	#STARTX*2*2,A1
LOOPCIR	MOVE.W	#RADIUS*RADIUS,D0
	MOVE.W	D2,D1
	MULS	D1,D1
	SUB.W	D1,D0
	BSR.S	SQR		; SQR(R²-X²)
	MOVE.W	D0,(A0)+
	MOVE.W	D0,-(A1)
	SUBQ.W	#1,D2
	BNE.B	LOOPCIR
	RTS
SQR	TST.W	D0
	BEQ.S	.EXIT
	MOVE.L	D1,-(SP)
	MOVE.W	D0,D1
	CMP.W	#64,D1
	BHI.B	.SQR1
	LSR.W	#3,D0
	ADDQ.W	#2,D0
	BRA.S	.SQR4
.SQR1	CMP.W	#1024,D1
	BHI.S	.SQR2
	LSR.W	#5,D0
	ADDQ.W	#8,D0
	BRA.S	.SQR4
.SQR2	CMP.W	#8192,D1
	BHI.S	.SQR3
	LSR.W	#7,D0
	ADD.W	#32,D0
	BRA.S	.SQR4
.SQR3	LSR.W	#8,D0
	CMP.W	#32767,D1
	BHI.S	.SQR4
	ADD.W	#64,D0
.SQR4	EXT.L	D1
	DIVU	D0,D1
	ADD.W	D1,D0
	LSR.W	#1,D0
	MOVE.L	(SP)+,D1
.EXIT	RTS

** MAKE A TABLE FOR EACH ZOOM VALUE *******************************************

COPYCIR	LEA.L	TABLE(PC),A0
	LEA.L	REPLICA(PC),A1
	MOVEQ	#0,D0		; ZOOM
	MOVE.W	#87,D4
CGLOBAL	ADDQ.W	#1,D0
	MOVE.W	#RADIUS*256,D2
	DIVU	D0,D2
	EXT.L	D2
	MOVE.W	#2*87,D3
CLOOP	MOVE.W	(A0)+,D1
	LSL.W	#8,D1
	DIVU	D2,D1
	MULU	#24,D1
	MOVE.W	D1,(A1)+
	SUBQ.W	#1,D3
	BNE	CLOOP
	LEA.L	TABLE,A0
	SUBQ.W	#1,D4
	BNE	CGLOBAL
	RTS

** REALTIME VECTOR ZOOM BALLS *************************************************

VECTOR	JSR	ANGLES

	MOVE.W	X1,D0
	MOVE.W	Y1,D1
	MOVE.W	Z1,D2
	MOVE.L	SCREEN_ADRESS,A1
	move.l	a1,a5
	SUB.W	XCENTER(PC),D0
	SUB.W	YCENTER(PC),D1
	SUB.W	ZCENTER(PC),D2
	BSR.W	ROTATE
	ADD.W	ZCENTER(PC),D2
	JSR	CONVERT
	ADD.W	XCENTER(PC),D0
	ADD.W	YCENTER(PC),D1
	MOVE.W	D0,SAFEX
	MOVE.W	D1,SAFEY
	SUB.W	#90,D0
	EXT.L	D2
	DIVU	#7,D2
	MOVE.W	#88,D3
	SUB.W	D2,D3
	MOVE.W	D3,ZOOM
	JSR	BALL
	JSR	CLRTAIL

	MOVE.W	X2,D0
	MOVE.W	Y2,D1
	MOVE.W	Z2,D2
	MOVE.L	SCREEN_ADRESS,A1
	ADD.L	#[336/8*256],A1
	move.l	a1,a5
	SUB.W	XCENTER(PC),D0
	SUB.W	YCENTER(PC),D1
	SUB.W	ZCENTER(PC),D2
	BSR.W	ROTATE
	ADD.W	ZCENTER(PC),D2
	JSR	CONVERT
	ADD.W	XCENTER(PC),D0
	ADD.W	YCENTER(PC),D1
	MOVE.W	D0,SAFEX
	MOVE.W	D1,SAFEY
	SUB.W	#90,D0
	EXT.L	D2
	DIVU	#7,D2
	MOVE.W	#88,D3
	SUB.W	D2,D3
	MOVE.W	D3,ZOOM
	JSR	BALL
	JSR	CLRTAIL

	MOVE.W	X3,D0
	MOVE.W	Y3,D1
	MOVE.W	Z3,D2
	MOVE.L	SCREEN_ADRESS,A1
	ADD.L	#2*[336/8*256],A1
	move.l	a1,a5
	SUB.W	XCENTER(PC),D0
	SUB.W	YCENTER(PC),D1
	SUB.W	ZCENTER(PC),D2
	BSR.W	ROTATE
	ADD.W	ZCENTER(PC),D2
	JSR	CONVERT
	ADD.W	XCENTER(PC),D0
	ADD.W	YCENTER(PC),D1
	MOVE.W	D0,SAFEX
	MOVE.W	D1,SAFEY
	SUB.W	#90,D0
	EXT.L	D2
	DIVU	#7,D2
	MOVE.W	#88,D3
	SUB.W	D2,D3
	MOVE.W	D3,ZOOM
	JSR	BALL
	JSR	CLRTAIL

	MOVE.W	X4,D0
	MOVE.W	Y4,D1
	MOVE.W	Z4,D2
	MOVE.L	SCREEN_ADRESS,A1
	ADD.L	#3*[336/8*256],A1
	move.l	a1,a5
	SUB.W	XCENTER(PC),D0
	SUB.W	YCENTER(PC),D1
	SUB.W	ZCENTER(PC),D2
	BSR.W	ROTATE
	ADD.W	ZCENTER(PC),D2
	JSR	CONVERT
	ADD.W	XCENTER(PC),D0
	ADD.W	YCENTER(PC),D1
	MOVE.W	D0,SAFEX
	MOVE.W	D1,SAFEY
	SUB.W	#90,D0
	EXT.L	D2
	DIVU	#7,D2
	MOVE.W	#88,D3
	SUB.W	D2,D3
	MOVE.W	D3,ZOOM
	JSR	BALL
	JSR	CLRTAIL

	RTS

** BLIT BALL ******************************************************************

BALL	SUB.W	#8,D1
	ADD.W	#15,D0

	MOVE.W	#%0000100111110000,D6
	TST.W	D0
	BGE.B	.OK
	MOVEQ	#0,D0
.OK	MOVE.W	D0,D2
	EXT.L	D2
	DIVU	#16,D2
	MOVE.W	D2,D0
	SWAP	D2
	ROR.W	#4,D2
	AND.W	#$F000,D2
	OR.W	D2,D6	
	MULU	#2,D0
	ADD.W	D0,A1
	MULU	#42,D1
	ADD.L	D1,A1	
	MOVEQ	#0,D0
	MOVEQ	#0,D2
	MOVEQ	#0,D3
	MOVE.W	ZOOM,D0
	LSL.W	#1,D0		; D0 = 2*RADIUS = HIGHT
	MOVE.W	#RADIUS*256,D2
;	tst.w	zoom
;	beq	null
	DIVU	ZOOM,D2
null	EXT.L	D2		; D2 = MAX BALL-DATA OFFSET
	MOVE.W	D2,D3
	LSL.W	#1,D3		; D3 = TABLE FACTOR
	LEA.L	BALLDAT,A0	; BLIT FIRST LINE WITH ALL SETTINGS
	JSR	BLTLINE
	SUBQ.W	#1,D0
	LEA.L	REPLICA,A2
	MOVE.W	ZOOM,D7
	MULU	#2*2*87,D7
	ADD.W	D7,A2

	MOVE.L	#$DFF000,A4
	MOVEQ	#0,D5
.BLOOP	LEA.L	BALLDAT,A0
	ADD.L	D3,D5	
	MOVE.L	D5,D4
	LSR.L	#8,D4
	BCLR	#0,D4		; ONLY EVEN ADRESSES
	EXT.L	D4
	MOVE.W	(A2,D4.W),D1	; D1 = SINUS
	ADD.W	D1,A0		; A0 = POINT TO BALL LINE
	BSR.W	BLTFAST		; BLIT LINE FAST
	SUBQ.W	#1,D0
	BNE.B	.BLOOP
	RTS

** BLIT BALL ******************************************************************

BLTLINE	BTST	#14,$DFF002
	BNE.B	BLTLINE
	MOVE.W	D6,$DFF040			; A SHIFT + MINTERM
	MOVE.W	#%1111111111111111,$DFF044
	MOVE.W	#%1111111111111111,$DFF046
	MOVE.L	A0,$DFF050			; SOURCE A
	MOVE.L	A1,$DFF054			; DESTINATION
	MOVE.W	#00,$DFF064			; MODULO A
	MOVE.W	#18,$DFF066			; MODULO D
	MOVE.W	#[1*64+192/16],$DFF058
	RTS

BLTFAST	BTST	#14,$2(A4)
	BNE.B	BLTFAST
	MOVE.L	A0,$50(A4)
	MOVE.W	#[1*64+192/16],$58(A4)
	RTS


** CLEAR BALL TAIL ************************************************************

CLRTAIL	MOVEM.L	D0-A6,-(A7)		

;	move.w	#$0fff,$dff180

WAIT2	BTST	#14,$DFF002		; CLEAR BUTTOM TAIL
	BNE.S	WAIT2
	MOVE.W	#$0100,$DFF040
	MOVE.W	#$0000,$DFF042
	MOVE.L	#$FFFFFFFF,$DFF044
;	MOVE.L	A0,$DFF054		; DEST = CONTINUE FROM BUTTOM OF BALL
	MOVE.W	#0,$DFF066
	MOVE.W	#[20*64+336/16],$DFF058
	MOVE.W	SAFEX,D0
	MOVE.W	SAFEY,D1
	SUB.W	#30,D1
	TST.W	D1
	BHI	OK2
;	MOVE.W	#0,D1
OK2	MULU	#42,D1
	ADD.W	D1,A5	
WAIT1	BTST	#14,$DFF002		; CLEAR TOP TAIL
	BNE.S	WAIT1
	MOVE.W	#$0100,$DFF040
	MOVE.W	#$0000,$DFF042
	MOVE.L	#$FFFFFFFF,$DFF044
	MOVE.L	A5,$DFF054
	MOVE.W	#0,$DFF066
	MOVE.W	#[23*64+336/16],$DFF058

;	move.w	#$0000,$dff180

OUT	MOVEM.L	(A7)+,D0-A6
	RTS

** ROTATE *********************************************************************

ROTATE	LEA.L	SIN_TABLE,A2
	LEA.L	COS_TABLE,A3
	MOVE.W	XVINKEL(PC),D5
	MOVE.W	YVINKEL(PC),D6
	MOVE.W	ZVINKEL(PC),D7
	LSL.W	#1,D5
	LSL.W	#1,D6
	LSL.W	#1,D7
	MOVE.W	(A3,D5.W),D3
	MOVE.W	(A2,D5.W),D4
	MULS	D1,D3
	MULS	D2,D4
	SUB.L	D4,D3
	LSL.L	#2,D3
	MOVE.L	D3,Y
	MOVE.W	(A2,D5.W),D3
	MOVE.W	(A3,D5.W),D4
	MULS	D1,D3
	MULS	D2,D4
	ADD.L	D4,D3
	LSL.L	#2,D3
	MOVE.L	D3,Z
	MOVE.W	(A3,D6.W),D3
	MOVE.W	(A2,D6.W),D4
	MULS	D0,D3
	MULS	Z(PC),D4
	SUB.L	D4,D3
	LSL.L	#2,D3
	MOVE.L	D3,X
	MOVE.W	(A2,D6.W),D2
	MOVE.W	(A3,D6.W),D4
	MULS	D0,D2
	MULS	Z(PC),D4
	ADD.L	D4,D2
	LSL.L	#2,D2
	SWAP	D2
	EXT.L	D2
	MOVE.W	(A3,D7.W),D0
	MOVE.W	(A2,D7.W),D4
	MULS	X(PC),D0
	MULS	Y(PC),D4
	SUB.L	D4,D0
	LSL.L	#2,D0
	SWAP	D0
	EXT.L	D0
	MOVE.W	(A2,D7.W),D1
	MOVE.W	(A3,D7.W),D3
	MULS	X(PC),D1
	MULS	Y(PC),D3
	ADD.L	D3,D1
	LSL.L	#2,D1
	SWAP	D1
	EXT.L	D1
	RTS

** CONVERT POINT FROM 3D TO 2D ************************************************

CONVERT	MULS	ZOOMB(PC),D0
	MULS	ZOOMB(PC),D1
	DIVS	D2,D0
	DIVS	D2,D1
	RTS

** CLACULATE NEW VIEW ANGLE ***************************************************

ANGLES	MOVE.W	YDELTA(PC),D0
	ADD.W	D0,YVINKEL
	CMP.W	#512,YVINKEL
	BLT.B	XANGLE
	SUB.W	#512,YVINKEL
XANGLE	MOVE.W	XDELTA(PC),D0
	ADD.W	D0,XVINKEL
	CMP.W	#512,XVINKEL
	BLT.B	ZANGLE
	SUB.W	#512,XVINKEL
ZANGLE	MOVE.W	ZDELTA(PC),D0
	ADD.W	D0,ZVINKEL
	CMP.W	#512,ZVINKEL
	BLT.B	ANGLOUT	
	SUB.W	#512,ZVINKEL
ANGLOUT	RTS

