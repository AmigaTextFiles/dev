	MOVE.W	#%0111111111111111,$DFF09A
	MOVE.W	#%0111111111111111,$DFF09C
	MOVE.W	#$0000,$DFF180
	MOVE.L 	$6C,OLDIRQ
	MOVE.L 	#MYIRQ,$6C
	JSR	INITCIR
	JSR	COPYCIR
	JSR	INITCOP	
	MOVE.W	#%1000001111000000,$DFF096
	MOVE.W 	#%1100000000110000,$DFF09A
