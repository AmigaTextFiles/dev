-----------------------------------------------------------------------------
		EIN NEAPOLITANISCHER IN FINNLAND FÜR DIE ASSEMBLY 95
-----------------------------------------------------------------------------

by PSYKO/MEGAHEAD

Dies ist mein Bericht über die ASM95, ohne das ganze Chaos, das ich machen
musste, bevor ich dort ankam. Tatsächlich war die Reise mit Abflug vom
Mailänder Flughafen geplant, aber für mich, der ich aus einer Stadt in der
Provinz Neapel stammte, ein echtes Abenteuer.

9. August 1995: Wir verlassen Mailand gegen 14:30 Uhr und kommen in Helsinki
um 18:00 Uhr an, nach einem Zwischenstopp in Frankfurt. Die erste Nacht
verbringen wir im Hostel, da die Party am nächsten Tag eröffnet wurde.

10. August 1995: Um 10:00 Uhr öffneten sich die Türen der Assembly 95. Dieses
Jahr fand es im Messezentrum in Helsinki (Finnland) statt, einige km vom
"Ice Palace", wo es im Jahr zuvor stattfand. Am Eingang mussten nach der
Ticketkontrolle "Armbänder" zur Wiedererkennung getragen werden. In der leeren
Halle standen Reihen von Tischen mit ausreichenden Steckdosen sowie solchen
zur Netzwerkverbindung. In etwas mehr als einer Stunde besetzten die
verschiedenen Gruppen die Tische mit ihren Computern, Bannern und sogar
Kühlschränken. Ich, der ich nur zu Besuch war, wurde der Gruppe Soft One (PC)
zugeordnet.
Ich war auf Patrouille und habe viel Asmone, Protracker und DeLuxe Paint auf
den Monitoren bemerkt. Die Musiker hatten Synthesizer und riesige Truhen. 
Viele haben ihre Produktion in letzter Moment beendet, während andere Gloom
spielten, seriell verbunden. In der Zwischenzeit liefen die Demos, die auf
den letzten Partys wie Gathering95 und The Party4 veröffentlicht wurden. 
Die Halle hatte keine Fenster, so dass die Projektion auf die Großbildleinwand
durch das Ausschalten des Lichts gut war. Da man aber nicht hinaussehen konnte,
wusste man nicht, ob es Morgen oder Abend war.
Insgesamt gab es 4250 Teilnehmer, über 700 Einsendungen, mit insgesamt 600 MB.
Die 80x86-Computer waren 431, der Amiga 83, das ist eine Netto-Minderheit
(1 Fünftel!). Wir können also sagen, dass die Assembly eine Party ist, wo der
PC König ist, im Gegensatz zur dänischen Weihnachts "PARTY", wo der Amiga
immer noch die Vorherrschaft hat.
Darüber hinaus wurden mehrere GigaBytes aus dem lokalen Netz an das Internet
angeschlossen. Diejenigen, die an der Fertigstellung ihrer Produktionen
arbeiteten, hatten bis 23:00 Uhr des nächsten Tages Zeit (mit Ausnahme derer
vom C64, die nur bis 14:00 Uhr Zeit hatten).

11. August 1995: um 10:00 Uhr ist auch eine spezielle Veranstaltung zu  
Rollenspielen (RopeCon) geöffnet, die sich in der nahegelegenen Lobby befinden.
Am Nachmittag war der Fußballwettbewerb, an dem ich teilgenommen habe, und
Diskuswurfen (der Gewinner hat 100 Meter überschritten!).
Am späten Nachmittag hatte ich die Ehre, einen der bekanntesten Programmierer,
zu treffen und zu fotografieren nämlich Dr. Skull / VD, der wie eine Blondine
aussieht eher dünn, der mir sogar ein Autogramm gegeben hat.
Gegen 20:00 Uhr begann der C64-Wettbewerb, bei dem man Effekte wie
Typ-Texturen und mehr zu sehen waren, zum allgemeinen Erstaunen, aber war das
Niveau nicht höher als im letzten Jahr. Vielleicht haben sie das Maximum des
C64 erreicht? Bis zum Zeitpunkt der DeadLine (der Zeit der Abgabe, d.h. 23:00)
hat man viele Gruppen im kalten Schweiß gesehen, die dabei waren ihre
Produktion zu beenden.
Beachten Sie, dass der Großteil der Jury während der Party gebildet wurde, dann
wurden die Anwesenden (über die große Leinwand) gebeten, daran teilzunehmen.
Dies führte zur Verbreitung von Gerüchten, über die Seriosität der
Organisation und der ersten Auswahl. Zwischen der Nacht und dem nächsten Morgen
prüfte die Jury die eingegangenen Intros, Demos, Musik und Bilder durch und
entschied, welche von ihnen am Finale teilnehmen.

12 August 1995: Um 12:00 Uhr startete der 4-Kanal-Wettbewerb von musikalische 
Module: im Finale waren viele Techno-Stücke zu hören, einige von ihnen
erinnerten an den Klang eines Hammers, der auf Metall schlägt.
Um 13:00 Uhr begann der Wettbewerb der Grafiken, wo Bilder von ausgezeichneter
Qualität zu sehen waren, beide Bilder, die das Finale sicherlich nicht verdient
haben.
Gegen 14:00 Uhr startete der 32-Kanal-Wettbewerb, bei dem es wirklich Lieder 
mit hoher Qualität gab.
Um 15:00 Uhr war der Wettbewerb der Bilder in Raytracing an der Reihe, bei der
ich einige Bilder gesehen habe, darunter auch das eines Italieners, das zu
Unrecht bestraft wurde, um Platz für schreckliche Bilder in der Endrunde zu
machen.
Um 16:00 Uhr wurde der PC-Wettbewerb der 4Kb-Intros gestartet, wo ich ein
wirklich fantastisches gesehen habe, das fast in RealTime zwei kleine
Animationen in Raytrace berechnet hat.
Um 17:00 Uhr begann schließlich der Intro-Wettbewerb für den AMIGA, bei dem
wenn man die ersten vier Platzierten ausnimmt, die Qualität der Produktionen
nicht sehr hoch war . Allerdings gab es eine, die in Multitasking ging!
Um 18:30 Uhr waren die PC-Intros an der Reihe, danach ging der Tag zu Ende.
Während der Nacht überprüften sie alle Produktionen der Compo.

13. August 1995: Um 10:00 Uhr startete nach langem Warten der Demo-Wettbewerb
des AMIGA. Am Anfang wurden einige mittelprächtige Demos von aufstrebenden
Gruppen gezeigt, aber gegen Ende traten historische Gruppen des AMIGAs mit
einigen fantastischen Demos auf, darunter Fruit Kitchen von Silents und
Zif von Parallax, der den ersten Platz belegte.
Diese letzte Demo hat einige neue Routinen, zum Beispiel eine neue Textur
Mapping, das andere Texturen widerspiegelt, als ob die Oberfläche metallisch
oder glatt wäre, dazu gab es Bump-Mapping und die die Projektion von farbigen
Lichtern auf 3D-Volumenkörpern sowie die üblichen Warping-Effekte.
Um 12:30 Uhr ging es um die Komposition der Animationen, und um 13:00 Uhr fand
schließlich der Demo-Wettbewerb für PC statt, bei dem eine italienische Gruppe
(Japotek) ins Finale kam und den zehnten Platz belegte.
Zur Stimmabgabe musste man eine Diskette verwenden, die am Eingang abgegeben
wurde, und 5 Produktionen pro Kategorie wählen. Nach den Wettbewerben wurden
die Disketten zurückgezogen, um die automatische Berechnung durchzuführen.
Um 16:30 Uhr gab es das am meisten erwartete Ereignis, nämlich die Bekanntgabe
und die Preisverleihung der ersten drei Plätze in jeder Kategorie. Sie fand in
dem Raum statt, in dem die IT-Stände aufgebaut waren, wo Sponsoren Artikel
verkauften oder, wie im Fall von Nintendo, neue Produkte anpriesen.
Während wir an der Preisverleihung teilnahmen, wurden einige Jungs und ich von
Reportern einer Zeitung aus Helsinki fotografiert und interviewt, andere Jungs
landeten stattdessen vor den Kameras, die das offizielle Video der ASM95
aufnahmen.

