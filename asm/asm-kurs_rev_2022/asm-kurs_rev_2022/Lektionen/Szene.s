******************************************************************************
			Die Situation in Italien
******************************************************************************

In Italien findet das Treffen normalerweise auf der SMAU in Mailand statt, der
jährlichen Computermesse im Oktober. Wegen der geringen Anzahl und der
fehlenden Sponsoren kann man keine PARTY veranstalten, wie sie im Ausland üblich
ist. Persönlich habe ich für die SMAU 94 in Zusammenarbeit mit der Gruppe NOVA
ACIES eine Demo namens NAOS veröffentlicht, während auf der SMAU 93 meine erste
AGA-Demo veröffentlichte wurde, die WORLD OF MANGA, in Zusammenarbeit mit der
Gruppe KOGA DESIGN.
Obwohl sich die aktivsten Gruppen in Nordeuropa befinden, gab es und gibt es in
Italien noch einige gültige Gruppen: Ich kann zum Beispiel benennen: the
Italian Bad Boys (IBB), Half Brains Team (HBT), RAM JAM, SKANDAL, NOVA ACIES,
KOGA DESIGN, NightFall, DIVINA, AESTHETICA, NULL DEFEKTE, TEF, nämlich The Evil
Forces und viele andere. Jede dieser Gruppen hat eine oder mehrere Demos
erstellt, von denen viele in den Listen britischer PD-Unternehmen sind.
Derzeit scheint es, dass neue Gruppen entstehen, allen voran BSD, das heißt
BIOSYNTHETIC DESIGN, welche TENEBRA veröffentlichte. Andere Gruppen schließen
sich zusammen, wie MegaHeads, Ozones und andere.
In Italien werden 2 MAGAZINE produziert: ABNORMALIA und THE CHARTS von RAM JAM.
Erst kürzlich ist ein neues italienisches Diskmagazin erschienen, INFAMIA.
Leider haben sich viele Gruppen aus verschiedenen Gründen getrennt, aber das
eigentliche Problem ist, dass keine neuen gebildet werden, es keinen Ersatz
gibt. Dies ist vielleicht auf die Tatsache zurückzuführen, dass die alten
Gruppen mit Erfahrung vom C64 kamen, während die neuen Amiga-Besitzer keine
andere Computer hatten. Auf einem Computer wie dem alten C64 kam das
Programmieren fast spontan, da es beim Einschalten die BASIC-Sprache hatte. Bei
moderneren Computern ist das Betriebssystem viel intuitiver, also müssen sie
nicht einmal die Tastatur berühren, um Operationen auszuführen, was die
Benutzer fauler macht. Auch war es auf dem C64 praktisch Pflicht, die
Assembler-Sprache zu lernen, um anständig zu programmieren, angesichts der
Einschränkungen von BASIC, während sich auf dem AMIGA viele entscheiden,
Sprachen wie AMOS oder C zu lernen, die nicht schnell und mächtig genug sind,
um Action-Spiele oder Demos zu machen.
Unter so vielen Gruppen, die sich auflösen, scheint es, dass nur ein paar
neu gegründet werden, es sind die Lustrones, die die "SING SING"-Party
abgehalten in Mailand am 29. März 1995 mit der Demo "UNCODED" gewonnen haben
und den Morbid Visions, die vorerst nur ein BBS INTRO veröffentlicht haben.
Biosynthetic Design bereitet auch die erste Demo vor.
Auch in der Hoffnung, das Aussterben der italienischen SZENE zu verhindern,
habe ich zusammen mit anderen Demo- und Spieleprogrammierern den Kurs
geschrieben !!

