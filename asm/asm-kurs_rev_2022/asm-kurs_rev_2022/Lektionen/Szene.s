Sie sind die Versammlungen des Demos-Kultes, die in Assembler programmiert
sind. Es überrascht nicht, dass zwei wichtige PARTYS, die jedes Jahr
organisiert werden, bezeichnet werden als GATHERING (Versammlung) die in
Norwegen stattfindet, und die ASSEMBLY, die in Finnland stattfindet. Dann gibt
es noch die Saturne in Frankreich und das Weihnachtsfest in Dänemark.
"Kleine" Partys finden in Polen, Deutschland, England und in allen Ländern, in
denen die Szene existiert statt. Diese PARTYS sind eine Chance, andere
Mitglieder der Szene zu treffen die Sie sonst nie kennengelernt hätten.
Da es Preiswettbewerbe gibt, warten die stärksten Gruppen normalerweise auf
eine Party, um ihre Demos zu veröffentlichen, deshalb sind sie eine sehr
wichtige Sache in der ELITE-Szene.
Die Partys finden in geräumigen Umgebungen statt, z.B. in Sporthallen oder
Konferenzräumen und die große Leinwand (BIG SCREEN) darf nicht fehlen, auf dem
die Demos dem Publikum gezeigt werden (bei den wichtigsten Partys sogar 
Tausenden von Menschen). Angesichts der Verwirrung wird der Bildschirm auch für
wichtige Mitteilungen, wie die Rangfolge der Gewinner, verwendet. Beachten Sie,
dass die Organisatoren eine Reihe von Tischen mit Steckdosen zur Verfügung
stellen, und dass die Teilnehmer ihren eigenen Computer mitbringen und
aufstellen müssen. Die Tische sind also mit Amigas und anderen Teufelszeug,
einschließlich Bannern, Lautsprechern usw. voll.
