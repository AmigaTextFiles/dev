Schon mal was von Gruppen, Szenen, Partys gehört?
Wenn ich versuche zu beschreiben, was die Szene ist, werde ich sicherlich
jemanden mit einem Einwand finden. Auf der anderen Seite bin ich es irgendwie
schuldig zu beschreiben. Also ... die SZENE ist hier als "AMIGA-SZENE" zu
verstehen, da es auch andere Szenen gibt, wie die "PC SZENE", der unglücklichen
Besitzer von MS-DOS. Es gab die "C64 SZENE" und in Wirklichkeit gibt sie immer
noch, ein Verdienst von 64er Neugründern, die weiterhin Demos für die herrliche
Kiste programmieren. Auf den ersten Blick mag es so scheinen, dass der einzige
gemeinsame Nenner zwischen diesen drei Szenen der ist, dass es sich um eine
Ansammlung von Menschen handelt die Computermaterial produzieren und
austauschen. Ja, sie PRODUZIEREN es, dies ist ein grundlegender Unterschied
zu USERN, die nur die Software benutzen. Natürlich kann die Produktion in Form
von nützlichen Programmen (utilitys) von Demos oder sogar von Bildern und
"losen" Musikmodulen erfolgen.

Auf diese Weise wird die Szene in Programmproduzenten (Coder), in Designer 
(GFX-Künstler), Musiker unterteilt und diejenigen, die sich mit der massiven 
Verbreitung solcher Produkte befassen, das heißt SWAPPERS und TRADERS, die
es per Post oder per Modem verschicken.
Beachten Sie, dass die Musiker fast ausschließlich mit dem Programm
"PROTRACKER" Musik machen, da es eine leistungsstarke und schnelle
Wiedergaberoutine hat. Grafikdesigner zeichnen normalerweise freihändig
mit DPaint oder Brilliance. Nicht sehr beliebt sind diejenigen, die
Zeichnungen einscannen oder mit 3D-Programmen gerendert werden, aber einige
nutzen auch diese Methoden, die meiner persönlichen Meinung nach nicht strafbar
sind, solange man eine EIGENE Zeichnung und etwas Anständiges rendert.
Wenn man anfangs weder programmieren kann noch zeichnerisch begabt ist oder
Musik machen kann, er kann Händler werden und dann mit der Zeit wer weiß ...
Händler handeln Demos per Mail oder Modem, und einige Gruppen haben einen
eigenen BBS (Bullettin Board System), aufgestellt bei jemandem zu Hause, einem
Mitglied, der über einen Computer verfügt, der nur für diesen Zweck verwendet
wird. Derjenige, der sich um die BBS kümmert, heißt SYSOP. Beachten Sie, dass
es viele Arten von BBS gibt, und nur ein kleiner Teil davon ist Teil der Szene.
Die Mehrheit sind BBS von "normalen" Amiga-Enthusiasten, nicht Teil von
Gruppen, die oft professionelle Grafikdesigner oder szenefremde User sind und
sie machen Zeichnungen, Musik nur für sie und ihre Mutter. Aber diese sind oft
sehr gut und es wäre toll, wenn sie mit der Szene in Kontakt kämen. Aktuell,
mit dem Aufkommen des Internets, kolonisiert die Szene auch diesen Cyberspace
und Sie können Sites finden, die von Gruppen verwaltet werden.
Beachten Sie, dass eine der Fixierung der "Amiga-Szene" darin besteht, der
"PC-Szene" zu demonstrieren, dass der Amiga besser ist als MS-DOS, und
natürlich antwortet die PC-Szene mit klapprigen Demos, die einen 100 mal
schnelleren Prozessor benötigen, um (fast) das gleiche zu tun. Sie sind so
schnell, um (fast) das Gleiche zu tun wie ein Amiga500, das sich dann als
20MB lange Animationen herausstellt, die weder unter Windows, noch mit  
erweiterte Speichermanager funktionieren.... hahahaha!
Demo ist eine Abkürzung für "Demonstration" und dies deutet darauf hin, dass
solche Programme nicht darauf ausgelegt sind, ein Problem zu lösen, im
Unterschied zum Beispiel zu einer Datenbank oder eines Antivirenprogramm, die
entwickelt wurden, die Festplatten zu archivieren bzw. zu "bereinigen".
Aber es gibt auch Leute, die scheinbar ohne ein bestimmtes Ziel mit dem
Programmieren anfangen, nur um des Programmierens willen. In diesem Fall kann
das Ergebnis nur eine Demo sein. Eine Funktion der Demos ist, dass sie
anscheinend nutzlos sind, das ist einer der Gründe ihrer begrenzten
Verbreitung. Einige Freunde waren fassungslos als wir sie fragten, ob sie
wollten, dass wir ihnen Demos kopieren, und sie haben geantwortet: "Kannst du
nicht mit ihnen spielen?" Sie mögen schön sein, aber sie zeigen kein Interesse.
Stattdessen wenn es um ein Spiel geht, auch wenn es das hässlichste und
langweiligste der Welt ist, man würde auch einen Pakt mit dem Teufel schließen,
um es zu haben.
Aber diese SZENE wurde vorher nicht HOBBY genannt? Nun ... die Frage ist
spontan entstanden und die Antwort ist da. Schauen wir uns jemanden an, der ein
Hobby hat, zum Beispiel Briefmarken sammeln.
Er wird seine Zeit bei der Suche nach Briefmarken polarisieren, und er wird
eine tolle Kultur zum Thema haben, da er sich wohl Kataloge oder Bücher kaufen
wird. Aber er bleibt allein, um über seine Lieben Briefmarken nachzudenken und
nebenbei erstellt er nichts, sondern nur Archive und außerdem ist es sehr
einfach, da seine Arbeit hauptsächlich auf dem Weg zur Post oder zur Philatelie
liegt um hin und wieder etwas zu kaufen.
Er kann höchstens ein paar Freunde haben, mit denen er Duplikate austauschen
kann und hier ist er ein wenig "besser", aber Tatsache bleibt, dass sie die
Briefmarken nicht "machen".
Dieses Hobby kann man mit der Aktivität jener Amiga-Besitzer verglichen werden
die ab und zu in den Laden (oder zum Piraten) gehen, um neue Spiele zu kaufen,
und sie sammeln, um mehr über die geheimen Moves von Mortal Kombat zu erfahren
in Artikeln in Videospielmagazinen. Ähnlich wie ein Sammler, kann er
einsam sein oder ein paar Freunde haben mit denen er gelegentlich die Disketten
tauscht. Die maximale Apotheose, die mit diesen Disketten erreicht werden kann,
ist, dass sie einen Club mit einen Dutzend "Aktivisten" gründen.
Stattdessen besteht die Szene aus Tausenden von Menschen auf der ganzen Welt
und das ist schon ein relevanter Unterschied.
Aber kommen wir zu einigen "kreativeren" Hobbys. Zum Beispiel könnten die
Liebhaber von Fotografie oder Comics auch ihre eigenen Fotos machen oder
ihre Bilder zeichnen, wenn auch "bestenfalls".  Sie haben vielleicht gute
MEISTER zu folgen, nämlich den besten Fotografen und Designern, und streben
danach diese Ebenen zu erreichen. Außerdem könnten sie sich treffen, um ihre
Arbeit und die ihrer Idole auszutauschen.
Das sind Hobbys, die ein bisschen mehr nach SZENE aussehen, da es Menschen
sind, die eine Leidenschaft für etwas haben, die auch versuchen etwas zu
schaffen oder etwas neues erstellen, sowie sammeln. Aber auch mit einem
FAN CLUB kommt man nicht auf mehr als hundert Mitglieder und alle aus der
gleichen Gegend.
Stattdessen hat sich die Arbeit eines Szenemitglieds einmal verbreitet wird
es vom Rest der Szene gesehen (und leider beurteilt), das heißt von Tausenden
von Menschen überall, von Polen bis Spanien, von Finnland nach Südafrika (wobei
offensichtlich die zentralafrikanischen Länder übersprungen werden, die nicht
das Glück haben, an etwas anderes zu denken, als zu überleben).
Wenn ich Fotograf wäre, wie könnte ich all die Fans dazu bringen, zu kommen?
Weltfotografie mein Job? Bohhh...
Daher ist die Szene eine "Gesellschaft", mit eigenen Gesetzen und eigenen
Geheimnissen, der in verschiedenen Ländern der Welt lebt, als ob es ein
einziges Land wäre. Die "Bräuche" und die "Kultur" der Szene wurden damals
mit den ersten Computern, wie dem Commodore 64, geformt und gefestigt und noch
heute sind es dieselben. Aber wie und wann wurde das Demo-Phänomen geboren?
Als der Computer für Nicht-Programmierer zugänglich wurde, seltsamerweise!
Als Computer nur in Rechenzentren eingesetzt wurden, dachte niemand jemals
daran, nutzlose Programme zu machen. Aber am Ende der siebziger Jahre begannen
Computer für jedermann verfügbar zu sein und dies verursachte das Auftreten von
seltsamen hausgemachten Programmen. Sie spiegelten die schlechten
Programmierkenntnisse und seltsamen Ideen der Autoren. Unter diesen glorreichen
Computern ist der Commodore 64 der beliebteste. Es ist tatsächlich so, dass für
diesen Computer, die ältesten Demos gefunden werden können.
Da fast alle Amiga-Demoautoren mit dem C64 angefangen haben, ist es notwendig,
sich mit diesen Pionieren zu identifizieren, um die Entstehungsgeschichte der
sogenannten "SZENE" vollständig zu verstehen, dh einer Reihe von "GRUPPEN"
von Demo-Autoren und PD-Dienstleister, die jedes Jahr Material austauschen und
die "PARTY" organisieren, oder Partys, bei denen die verschiedenen Gruppen in
der Umgebung oder sogar aus anderen Ländern wiedervereinen, um ihre neuesten
Produktionen zu veröffentlichen. Zwei berühmte Partys sind die ASSEMBLY, die in
Finnland stattfindet, und die Weihnachts-Party in Dänemark. Die Szene ist in
Gruppen unterteilt, die auch Untergründe haben können Abschnitte, und jede
Person hat ein Handle, normalerweise ein Pseudonym in englischer Sprache,
welches von der Person selbst gewählt ist. 
