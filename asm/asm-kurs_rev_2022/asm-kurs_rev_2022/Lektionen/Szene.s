wollen wissen warum RANDY? Weil es kurz ist und "Bösewicht" bedeutet, geil und
chaotisch", was mir zumindest für die geilen Leute zusagt).
Oft kann man am Handle die "Spezialisierung" und Nationalität des
"Szenenmitglieds" erraten, zum Beispiel verzerren die Musiker das Wort "Audio"
oder die Namen berühmter Musiker usw.
Warum werden Handle verwendet? Vor allem, weil es auf diese Weise leichter ist
jemanden zu erkennen und sich zu merken, wie der So-und-So heißt ... stellen
Sie sich vor, wie schwierig es ist, sich norwegische, finnische Namen zu
merken...
Auf das Handle folgt immer der Name der Gruppe, zu der es gehört, zum Beispiel
mein volles Handle ist RANDY / RAM JAM, da ich jetzt in dieser Gruppe bin.
Ich war schon in anderen Gruppen, also war mein Handle RANDY / NOVA ACIES,
RANDY / KOGA-DESIGN usw. Der Übergang von einer Gruppe zur anderen kann
verursacht sein durch EXPULSION (Kick), wobei man "zu Fuß" bleibt, oder durch
den Ausgang freiwillig geht (vorerst hat mich niemand rausgeschmissen, obwohl
ich arrogant und unhöflich bin).
Bevor man sich einer Gruppe anschließt (d.h. JOINING), oder nachdem man 
herausgekickt wurde sind Sie UNABHÄNGIG, ob Sie es wollen oder nicht.
Dies muss jedoch genau festgelegt werden! Zum Beispiel: RANDY / INDEPENDENT.
Die Szene ist ein ständiges Miteinander, ohne Ausnahme von Tritten für
diejenigen, die etwas falsch tun (oder als solche beurteilt), für die es
leicht ist, "den Faden" zu verlieren, und mit TIZIO / BAU Antworten auf den 
Brief an TIZIO / MIAO.
Wahrscheinlich ist die BAU-Gruppe "tot", dh ihre Mitglieder sind müde zu
produzieren, und die wenigen, die weitermachen wollten, schlossen sich anderen
Gruppen an oder sie gründeten eine neue Gruppe, die MIAO sein könnte.
Manche ändern auch den Handle, aber das sind (zum Glück) recht seltene Fälle.
Anzumerken ist, dass wir hier über das Hobby hinausgehen, denn wenn ich was für 
die Szene mache, bin ich nicht mehr Fabio Ciucci, sondern Randy / Ram Jam, ja
es führt eine echte Spaltung der Persönlichkeit durch. Deshalb neigen wir dazu
zu sagen, dass die Szene ein "Kult" ist. 
Kehren wir zu der wenig bekannten Geschichte der Demos zurück. Vor einiger Zeit
haben die Pioniere der C64-Programmierung angefangen, sich mit ihren Programmen
auseinanderzusetzen, komisch: na ja, es fing wohl alles damit an, als jemand,
der 6 Sprites gleichzeitig auf dem Bildschirm bewegt hatte, einen Freund fand
der 7 bewegen konnte. Diese beiden hypothetischen Freunde forderten sich immer
wieder in ähnlichen Fähigkeiten heraus, als sie wahrscheinlich auf exotischere
Dinge umgestiegen sind, um den Gegner zu überraschen, zum Beispiel um die
Sprites unter den Rändern loszulassen, um sie fast aus dem Monitor zu bekommen,
oder mehr als 16 Farben auf dem Bildschirm anzuzeigen (das Maximum für einen
C64) mit den undenkbarsten Tricks.
Der Kampf bis zum letzten Byte aus der BASIC-Sprache ging bald bei den Meisten
über in das geeignetere ASSEMBLER, das die vollständige Kontrolle über den
Computer ermöglicht.
An diesem Punkt gehen die beiden Freunde vom Kampf zur Zusammenarbeit über, in
Versuchen, das Unmögliche zu tun. In der Zwischenzeit haben sie den Laden
besucht, in dem sie ihr kleines Juwel gekauft haben und zeigen von Zeit zu Zeit
ihre Kreationen mit unglaublich vielen hüpfenden Kugeln oder gleiten Schriften
(der SCROLLTEXT). Unter den vorbeigehenden Kunden treffen sie auf einen 
begeisterten Zeichner, der ihre Werke sieht und von diesem Moment an haben die
beiden Programmierer (im Jargon CODER), jemanden der die Grafiken zeichnet, die
sie in Bewegung setzen. Es dauert nicht lange und sie treffen auch einen
Musikfan, der anbietet mitzumachen.
An diesem Punkt treffen sich die vier immer öfter und geben sich zum Spaß
Künstlernamen und beschließen, ein Team, d.h. eine GRUPPE, zu bilden, und 
bestimmen deren Namen. Äußerst exaltiert, bringen sie alle Zeichnungen und
Musik zusammen, um eine Art Sammlung aller Effekte zu programmieren, die
sie es geschafft haben und hinterlassen ihre Adresse im SCROLLTEXT. Das
Ergebnis ist so schön, und sie sind so stolz darauf, dass sie es allen Freunden
kopieren und es im Laden lassen. Nach ein paar Wochen bekommen unsere vier
einen Brief mit einer Kassette (Denken Sie daran, dass die Geschichte zu der
Zeit spielt, als das magnetische Medium die Audiokassette war) Sie laden das
Programm und bei seiner Ausführung sind sie überrascht, dass sich mehr Grafiken
gleichzeitig bewegen, als sie bisher machen konnten, und sie lesen im
SCROLLTEXT: "Hallo, wir sind die Mitglieder der ILLUSION-Gruppe, ....".
In einer nahegelegenen Stadt hatten sich andere zu einer Gruppe
zusammengeschlossen! Durch Kontaktaufnahme erfahren sie, dass ILLUSION wiederum
in Kontakt mit anderen Gruppen von anderen Städte und anderen Länder steht.
So entstand die "C64 SZENE", die sich dann weiterentwickelte und
diversifizierte in verschiedene Wege: Zunächst einmal haben einige Gruppen
begonnen, ihre Fähigkeit zu nutzen, die Schutzmechanismen von Spielen und
Programmen zu umgehen, meist um sich selbst herauszufordern, da es in ASSEMBLER
möglich ist, alles zu tun und rückgängig zu machen.
Andere haben es vorgezogen, die Spiele so zu verändern, dass es unmöglich ist
zu verlieren, indem sie die TRAINERS erfunden haben. Wieder andere haben
begonnen, Register zu verändern, indem sie ihnen Optionen hinzufügten. Mit der
steigenden Zahl der C64-Besitzer ist auch der Markt der Spiele gewachsen die
illegal von PIRATEN kopiert wurden, d.h. die spezialisierte Gruppen gebildet
haben deren Mitgliedern in der Lage waren den Schutz der Originalspiele
aufzuheben mit einem effizienten und profitablem Vertriebsnetz.
Es ist klar, dass diese Art von Gruppe nichts mit der oben beschriebenen Gruppe
zu tun hat. 
Auf der anderen Seite hat die schlechte Information dazu geführt den
Durchschnittsnutzer glauben zu lassen, dass alle Gruppen darauf abzielen, Geld
zu verdienen indem sie auf Kopien absehen. In Wirklichkeit ist eine klare
Unterscheidung zwischen den verschiedenen Gruppen geblieben, so dass man von zwei
getrennten "SZENEN" sprechen kann, nämlich von der "CRACK-SZENE", die von den
Piraten gebildet wird (CRACK bedeutet "knacken"), und die "DEMO-SZENE",
die die Demos produziert.
Dann gibt es Gruppen, die Demos machen und hin und wieder zum Spaß Programme
ungeschützt machen, aber weniger organisiert als die professionellen "sündigen"
Piraten.


