Das INTRO oder INSIDE sind "kleine" Demos, eine Reihe von zusammengestellten
Effekten, nicht genug, um eine großartige Demo zu machen, aber trotzdem
veröffentlicht. Dort sind einige spezielle Wettbewerbe für diese Kategorie auf
der PARTY. Die Länge wird gemessen, zum Beispiel kann die Grenze 40 KB oder
64 KB betragen. In letzter Zeit ist es umstritten sogar ein Intro-Wettbewerb
mit höchstens 4 KB!
Intros werden jedoch oft verwendet, um zum Beispiel über etwas zu informieren
die Geburt einer Gruppe, der Geburtstag eines Mitglieds, die Eröffnung von
einer BBS oder was auch immer. Manche nennen ihre Produktionen "IN" oder
"INTRO" anstatt sie "DEMO" zu nennen, aus Angst, es wird von den meisten der
ELITE verspottet, eine Art der "Bescheidenheit" des Neuen. Eine neue Gruppe muss
jedoch zuerst ein anständiges Intro, mit Musik, Grafiken und Code, dass von den
Gruppenmitgliedern selbst erstellt wurden ist erstellen. (wenn Sie anfangen,
die Grafiken, den Code oder die Musik von jemand anderem zu verwenden, ist es
das Ende), und im unvermeidlichen Scrolltext muss (auf Englisch!) erklärt
werden, dass die Mitglieder der neuen Gruppe XXX, YYY und ZZZ sind, die Adresse
um sie zu kontaktieren. Alle Beitrittsanfragen sind solche, wir suchen
Programmierer oder was immer sie wollen. 
Wenn Sie nicht in der Lage sind, ein Intro zu machen, weil Ihnen der Grafiker
oder der Musiker fehlt, dann müssen Sie jemanden finden oder versuchen, einer
nicht allzu elitären Gruppe beizutreten, indem sie ihnen Beispiele eurer
Fähigkeiten schickt. Aber ich denke, das Beste ist, eine eigene Gruppe in der
eigenen Stadt zu gründen und ein Intro zu erstellen, das dann in der BBS und
per Post verteilt wird, indem man sie an einige Mitglieder der Szene sendet und
hochlädt. Wenn das Intro nicht gut aussieht, schreiben Sie eine Entschuldigung
für die nicht außergewöhnliche Qualität in den Text und rechtfertigen Sie sich
damit, dass es Ihre erste Produktion ist.
Denken Sie daran, dass alle LAMER (halbe Patronen) waren, als sie anfingen: mit
Zeit und Engagement kommt man überall hin, es reicht die Etappen nicht mit
verschiedenen Täuschungen überspringen zu wollen.
Viele aus der "erhabenen" Elite-Szene werden sagen, dein Intro sei reine
