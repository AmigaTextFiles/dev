		DAS WISSEN DER SZENE DURCH DIE MASSEN
******************************************************************************

In den Augen derer, die keinen Computer haben oder ihn nur für langweilige
Abrechnungen verwenden sieht ein "Szenenmitglied" wie ein "Hacker" aus, gesehen
in irgendeinem Film, eine Art mysteriöser Zauberer, der seltsame Dinge tut,
aber das ist natürlich nicht die Realität der Szene. Andere "Experten"
identifizieren stattdessen Democoder mit Cracker, weil sie nur die "Crack-
Intros" in raubkopierten Spielen gesehen haben, aber auch in diesem Fall ist
(fast) alles falsch. Die SZENE wurde nie in italienischen Zeitschriften
erwähnt, abgesehen von ein paar Artikel über die Enigma Amiga Run (von mir
geschrieben) und andere Artikel über Demos in Spiele-Magazinen.
In all diesen Fällen war jedoch wenig Platz und oft war der Autor nicht selbst
ein Mitglied der Szene, konnte also keine genauen Kenntnisse über das Phänomen
haben. Die Demos kursieren jedoch immer mehr, sie sind in bestimmten Fällen auf
CD-ROM (zB euroscene1) zu finden, aber allenfalls hat jemand beiläufig eine
oder zwei Demos gesehen, ohne zu wissen, was sich dahinter verbirgt.
Zumindest ist bekannt, dass es eine Art von Software namens PD (Public Domain)
gibt, das für Public Domain steht und von Nutzern "inhouse" erstellt wurde und
frei kopierbar ist. Wenn Sie zum Beispiel ein Spiel planen und der Meinung
sind, es lohnt sich nicht es zu verkaufen, dann könnten sie es als PD
deklarieren und jeder kann es kopieren.

