könnten. Die AGA-Demos selbst funktionieren gut auf dem a1200, und es gibt
keine Programmierer, die für ihre Routinen, aufgrund ihrer Komplexität einen
68030 oder Fast Ram für a1200 benötigen. Das gleiche gilt für den
mathematischen Coprozessor, der von keiner Demo verwendet wird. Dann, wenn
jemand eine Demo macht, bei der Sie eine RAM-Erweiterung anschließen müssen
oder einen schnellerer Prozessor als den des Basiscomputers, über den wird
sich fast lustig gemacht. Im Scrolltext der Demos ist es nicht schwer zu lesen
an Coder adressierte Nachrichten, die sagen: "da du nicht deine Routinen
beschleunigen konntest, hast dir einen schnelleren Computer gekauft, hahaha!".
Ja, wenn man eine Routine gemacht hat, die auf einer 1200er-Basis nicht
reibungslos läuft, kann man eine Beschleunigerkarte oder zumindest etwas
Fast RAM kaufen, damit die Routinen reibungsloser ablaufen.
Aber er ist derselbe wie zuvor, es ist der Computer, der sich verändert hat.
Diese Hardware-Änderungsphilosophie ist die Regel für MSDOS-PCs.
Wenn man ein 3D-Spiel schneller ausführen möchte, kann man das letzte
Pentium Modell kaufen, was den Ladenbesitzer glücklich macht. Das Programm
bleibt langsam, aber das Ändern des Computers, auf dem es ausgeführt wird,
"verbessert" seine Geschwindigkeit. Stattdessen war auf dem Amiga (und C64)
der Trend immer ein und denselben Computer zu lassen, und die Routine zu
optimieren, auf diese Weise wird eine Denkweise geschaffen, die sich sehr von
der der PC-Benutzer unterscheidet.
