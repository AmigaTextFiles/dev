				PIRATERIE
******************************************************************************

Eine umstrittene Frage ist, ob sich die PIRATEN-SZENE wirklich von der
DEMO-SZENE unterscheidet. Nun, da es unter den gewöhnlichen Amiga-Benutzern
Leute gibt, die Spiele kopieren und Leute, die sie nicht kopieren, sondern
nur gelegentlich einige kaufen, gibt es auch in der Szene Leute, die
Raubkopien kopieren und andere, die sie nicht kopieren. Sie sind jedoch damit
beschäftigt die Demos und Diskmags zu kopieren, also "LEGAL STUFF", frei
kopierbar (ich sehe nicht was sie dagegen einwenden könnten, wenn ich MEINE
Demos an einen Freund von mir kopiere oder die Diskette mit dem Assembler-Kurs?
... ICH HABE ES GETAN!).
Gäbe es also die Piraterie nicht (absurderweise), würde die Szene mehr
Veränderungen sehen. In der Tat hätten die Leute vielleicht mehr Zeit, etwas
etwas Sinnvolles zu tun, anstatt zu spielen.
Die Verwirrung zwischen Piraten und Demo-Autoren wird vielleicht auch durch die
Tatsache genährt dass die Piratengruppen, die sich mehr aus Geldinteressen
gründeten, als auf dem für den Computer, sie haben oft keine Mitglieder, die
wissen, wie man ein CRACK INTRO (d.h. eine kleine Demo, die in raubkopierten
Spielen mit dem Namen der Gruppe) macht, also bitten sie einige Gruppen der 
"DEMO SZENE", ihnen ein solches zu erstellen.
Dies ist der Fall bei MELON, einer "ehrlichen" Gruppe, die Intros für CRYSTAL
gemacht hat. Wer einige Spiele kopiert hat, wird sich daran erinnern.
Der letzte grundlegende Unterschied zwischen den Piratengruppen und den
Demogruppen besteht darin, dass die Piraten unzählige Mailboxen/BBS haben, da
sie "dealen" müssen, im Gegensatz dazu haben die Mitglieder der DEMO-Szene
fast nie ein MODEM, da sie nicht so schnell einsatzbereit sein müssen wie die
Piraten. Zum Beispiel um Mitglieder berühmter Gruppen wie SANITY, SPACEBALLS
und KEFRENS zu kontaktieren ist der einzige Weg, ihnen zu schreiben.
Wir müssen also mit dem Glauben aufräumen, dass man durch die Verbindung mit
einer Mailbox mit den Programmierern der Demos oder Intros in Kontakt treten
kann: Sie haben das Intro wahrscheinlich an eine Gruppe, die nicht in der Lage
ist es zu programmieren, gesendet.
Einige Mitglieder der Szene, die illegales Kopieren nicht für unmoralisch
halten, arbeiten mit Gruppen zusammen, die auf Piraterie spezialisiert sind,
und geben ihnen eine Intro um es dem Spiel voranzustellen.
Andere verurteilen Piraterie, auch weil es oft vorkommt, dass aus
Demoprogrammierern Spieleprogrammierer werden.
Man kann Verwirrung stiften, indem man die Intros von Piratenspielen als
Beweis für die Zusammenarbeit der Demoszene mit der illegalen Szene sieht,
aber wie gesagt, ist die Piraterie in der Szene eine Tatsache, die nur eine
"physiologische" und von untergeordneter Bedeutung ist.
Erfahrenere und produktivere Gruppen wechseln oft zur Spieleprogrammierung
und Programme: Ein berühmter Fall ist der von TEAM 17, aber ich versichere
Ihnen, dass viele bekannte Spieleautoren das Programmieren in der Umgebung
der DEMO-SZENE gelernt haben.
Wenn Sie also eine Gruppe gründen oder Kontakt zu anderen aufnehmen möchten,
schreiben Sie an die Adressen, die Sie in den Demos finden, und seien Sie
vorsichtig mit denen in den Intros zu den Raubkopien von Spielen, wo man Leute
findet, die höchstens in der Lage sind, Kopierer zu sein.

