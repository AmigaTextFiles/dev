Allerdings sind verschiedene Formen nichtkommerzieller Programme zu 
unterscheiden:

PUBLIC DOMAIN: Der Autor vergibt alle möglichen Rechte an diesen Programmen.
 Jeder kann sie für jeden Zweck verwenden, es gibt keine Einschränkungen.
 In der Praxis verlangt der Autor keine Gegenleistung und wird nicht wütend,
 wenn sein Produkt beispielsweise von jedermann verwendet oder verändert wird.
 Es kann ohne Erlaubnis in den Code anderer Produkte aufgenommen werden, oder
 es kann als kommerzielles Produkt ohne Entschädigung für den Autor verkauft
 werden!
 Achten Sie also darauf, Ihre Kreation als PUBLIC DOMAIN zu deklarieren!
 Die einzige Möglichkeit, die Kontrolle über das Programm zu haben, besteht
 darin, es beim Urheberrechtsamt aufzunehmen und es in der Dokumentation zu	
 vermerken.

FREEWARE: Diese Programme sind frei kopierbar und verteilbar, aber der Autor
 hält weiterhin alle kommerziellen Verwertungsrechte, denn er erklärt das er 
 Eigentum an der Software und das Recht hat, die Regeln der Verteilung zu
 diktieren. Es ist urheberrechtlich geschützte Software, aber frei verteilbar.
 In der Praxis ist es ein "Verbrechen", die Diskette zu mehr als den Kosten
 der Diskette selbst zu kopieren. Obwohl der Autor keine Gebühr verlangt, ist
 es nicht gestattet, das Programm zu verkaufen und unverdient Geld durch die
 Arbeit des armen Programmierers einzustecken. Es ist auch "illegal", die
 Routinen dieser Programme zu stehlen, da das geistige Urheberrecht beim Autor
 liegt.
 Oft handelt es sich um FREEWARE-Vorschauen oder spielbare Demos von Spielen
 die für die Zukunft angekündigt und vermarktet wird. Das Risiko besteht darin,
 dass jemand die Routinen kopiert und Sie sie in einem seiner Programme oder
 Spiele verwendet und der Autor ist bei konkurrieren mit dem Programm eines
 anderen, das auch seine eigenen "gestohlenen" Routinen verwendet.
 Der Routinedieb könnte jedoch strafrechtlich verfolgt werden, insbesondere
 wenn das Programm beim Urheberrechtsamt registriert wurde.

SHAREWARE: Diese Art von Programm ist den FREEWARE-Programmen ähnlich, da sie
 nicht verändert werden sollten und die Verwertungsrechte beim Urheber liegen,
 aber bei Inanspruchnahme ist eine Zahlung erforderlich. Manche fragen zum
 Beispiel eine bestimmte Anzahl von Dollar ab, normalerweise von 5 bis 100, im
 Falle der Verwendung des Dienstprogramms. Um Sie zu "zwingen", die
 Anmeldegebühr zu zahlen, fehlen dem Programm oft Optionen, die mit der
 REGISTRIERUNG nur bei Zahlung dem Programm empfangen werden können. Natürlich
 zahlt nicht jeder die Gebühr. Es ist daher ein kommerzielles Produkt, aber mit
 einem Vertriebskanal nur im Zusammenhang mit der Verbreitung über BBs,
 Disketten in Zeitschriften oder Kopien zwischen Menschen, anstatt durch die
 Geschäfte. Einige fordern zur Zahlung der Gebühr auf und senden auch die
 Programmquelle, oder informieren registrierte Benutzer über neue Versionen. In
 der Praxis ist es so, dass die Benutzer das Programm testen können und diese
 können es nach kurzer Zeit kaufen oder kündigen. Wenn der Benutzer das zeitlich
 begrenzte Programm auf unbestimmte Zeit "ausprobiert", ist es so, als ob er es
 nicht mehr zurückgibt, ohne dafür zu bezahlen. Da es jedoch keine
 Zwischenhändler wie Geschäfte und auch keine Kosten für "Verpackung" gibt, sind
 die Kosten sehr niedrig, und in Wirklichkeit wäre es sehr bequem!

GIFTWARE: Dies ist eine Variante von Shareware, die darin besteht, dass man ein
 Geschenk oder ein relativ kostenloses Angebot als Entwicklungsunterstützung
 des Programms, anstelle eines festen Geldbetrags anfordert.
 Es handelt sich um Shareware mit nicht standardisierten Zahlungsmethoden.
 Dieser Kurs ist zum Beispiel eine Art GIFTWARE, da ich um etwas Geld
 und / oder Geschenke bitte. Was die Demos angeht, sie sind normalerweise als
 FREEWARE zu betrachten, die frei kopierbar sind, bei der aber die kommerziellen
 Verwertungsrechte beim Autor verbleiben. Eigentlich, wo Demos ohne 
 Startbildschirme vorhanden sind, die darauf hinweisen, dass es sich nicht um ein
 PD-Produkt handelt, und dass Sie mit ihrer Kopie kein Geld verdienen können,
 ohne die Autoren zu kontaktieren können sie als PUBLIC DOMAIN betrachtet werden.
 Leider "vergessen" Unternehmen, die PD-Software verkaufen, oft die Demo-Autoren
 zu kontaktieren, trotz der Warnung und verkaufen sie, ohne denjenigen, die sich
 an ihnen bereichert haben, etwas zukommen zu lassen.

