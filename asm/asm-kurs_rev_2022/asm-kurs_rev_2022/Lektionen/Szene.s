eine oder andere Weise, oft auf abwegigen und etwas mysteriösen Wegen.

Freude kann physisch oder psychisch sein.

Es kann ein Rausch bei einem Hochzeitsessen oder ein asketischer Rückzug in
eine Meditation sein, die Gesellschaft eines geliebten Menschen oder das lokale
Radio hören.

Es gibt auch Genussmittel, die besonders vielseitig und zufriedenstellend sind.
Der Computer ist ein perfektes Beispiel dafür. Der Besitzer benutzt ihn wie es
ihm beliebt, er füllt seine Festplatte mit vielen kleinen immateriellen
Werkzeugen, die er zu seinem Vergnügen benutzt, erforscht, kennt, konfiguriert,
gestaltet und es formt nach seinen Bedürfnissen und Wünschen. Vielleicht
verschönert er sie mit Pornobildern, um selbst die ureigensten Wünsche
teilweise zu befriedigen.

Jeder Benutzer neigt dazu, aus seinem Computer das perfekte Spielzeug zu
machen, das seine Bedürfnisse auf die von ihm bevorzugte Weise erfüllt.

Eine Quelle des psychologischen Vergnügens ist die Ego-Stimulation. Alles, was
Positives, über uns gesagt oder erwähnt wird, steigert unsere Meinung über uns
selbst, bläht das Ego auf, regt unseren Durst nach Anerkennung und Bestätigung
an.

Die Szene ist eine Welt der Ego-Stimulierung.

Die einzelnen Spieler in diesem großen Spiel handeln und produzieren für
Ego-Stimulierung, um den eigenen Namen durch die eigenen Werke zu bestätigen.

Im Gegensatz zu einem Computer kann die Szene nicht einfach nach nach den
eigenen Wünschen gestaltet werden, man muss sich anpassen um seine ego-
stimulierenden Ressourcen voll auszuschöpfen.

Die Suche nach Ego-Stimulation treibt Programmierer, Grafiker, Musiker und
all die anderen Spezies an, ihre Zeit mit Demos und diversen Produktionen
ohne Gewinn und ohne Anerkennung in der realen Welt. Sie zwingt sie dazu
sie nach den bestehenden ethischen und ästhetischen Kanons und Gesetzen zu
realisieren. Gleichzeitig veranlasst es sie zu versuchen, diese Kanons, die
nicht absolut sind, sondern sich mit der Szene entwickeln, eine aktive Rolle
auf ihr zu spielen, eine mit ihr das zu tun, was man mit einem Computer tut.

Die Szene ist gefälscht (fake), aber ihre Auswirkungen auf ihre Mitglieder sind
echt. Es ist ein Parallelleben, wo Persönlichkeiten erlebt werden, wo sie 
Erfolge erzielen können, die im wirklichen Leben nicht möglich sind.

Sie können sich in die Szene und das Leben in ihr verlieben, weil das reale
Leben nicht die gleichen Befriedigungen gewährt und nicht die gleichen
Ego-Stimulationen erzeugt.

Die Szene ist also gut für das Ego, aber sie ist so gefährlich wie die
Ego-Stimulation selbst. Sie ist eine Droge, die zu Toleranz und Abhängigkeit
führt. Die Stimulation wird immer in höheren Dosen gesucht, wohlwollende
Kommentare in der Szene über die eigene Arbeit wirken nicht mehr und lassen
einen gleichgültig, man hat sich daran gewöhnt.

Ego Stimulation ist die Basis der Motivation in der Szene, wenn sie nicht
mehr gegeben ist oder wenn höhere Dosen nicht mehr gegeben werden können, wird
der Szener müde, verliert das Interesse, verlässt die Szene.

Die Redakteure der wichtigsten Diskmagazine haben mehr oder weniger informell
beschlossen, eine massive Ego-Stimulationskampagne in der Szene zu starten. Da
wie wir wissen, die Amiga-Szene ist im Verfall begriffen ist, zu viele andere
Glühwürmchen locken ihre Mitglieder an, weniger Produktionen werden
vorbereitet. Es ist notwendig, die Szene zu schütteln, wir müssen die
Mitglieder motivieren, mehr zu produzieren und sich zu engagieren, wir müssen
ihnen geben, was sie brauchen, man muss das Ego stimulieren.

Ob und wann diese Redakteure Erfolg haben, wenn sie merken, das sie genug Macht
haben, um die Existenz der Szene und nicht nur einen Teil ihrer Dynamik selbst
zu beeinflussen, dann haben wir eine aktivere und gesunde Szene und eine Gruppe
von wenigen Leuten, die sich an der Macht berauschen und hoffnungslos das Ego
stimulieren.

Die Szene ist auch ein Mikrokosmos mit Regeln, die sich nicht allzu sehr von
denen der der realen Welt allzu sehr unterscheiden, nur dass das Spielen mit
ihnen nicht allzu sehr schmerzt.


