
SourceRescuer V1.0 	by Cuddley of Senseless

Questo programma ricerca in memoria, prima in fast, poi in chip, se ci sono
dei sorgenti. E' molto utile per ritrovare il nostro sorgente dopo che
il computer e' andato in guru resettandosi, cosa che accade spesso se si
programma "a rischio". Vi e' mai successo di fare un loop infinito e di
dover resettare per uscire, quando non avevate salvato il listato??
Ebbene, anziche' lanciare bestemmie e ripartire da capo, si puo' lanciare
questa utility! Naturalmente va lanciata subito dopo il reset o il guru
fatidico, o si rischia di sovrascrivere il listato.
USO: premere il bottone 'Start Searching'. Se trova un possibile sorgente,
si puo' vedere con "view" e/o salvare, scegliendogli un nome.
Se non era il sorgente giusto, si puo' continuare la ricerca premendo il
bottone 'Continue Searching'. Si puo' fermare la ricerca premendo il LMB,
ossia il Left Mouse Button.


