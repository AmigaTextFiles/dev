Learning 68k assembly – now or never
27.03.2021 von Lars Sobiraj Lesezeit: 10 Min.
Wanna learn assembler? Novel was so blown by RamJam's "Corso di Assembler",
so he discovered a few more opportunities that 2021 has to offer.

Novel was so blown away by the Amiga Compos after the 2019 revision that he
decided – once again – to learn assembler. This time he made it through the
first chapters of RamJam’s "Corso di Assembler" in no time – and discovered
a few more opportunities that 2020 has to offer for future Amiga coders.

A new old trend
The competitions at Revision 2019 and GERP 2020 proved it once again:
The Amiga is still alive and kicking and the Amiga scene is all the more
capable of setting standards in terms of design, concept and storytelling.
But above all, the stories of releases like EON by TBL or Coda by Abyss
show that there are still new tricks and methods to be discovered that can
impress the aging masses all over again.

