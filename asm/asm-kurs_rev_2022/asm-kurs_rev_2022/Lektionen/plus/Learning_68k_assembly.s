An English version is not yet known, but could one day become reality.
 "We did plan to make an English version," Yuki writes, "but the translation is
 really a huge job – 1872 pages of technical, informatic and mathematic
 explanations." They had considered paying for a professional translator, but
 "it’s complex to find a translator who knows assembly, Amiga, Motorola and the
 weird world of the demoscene".

Fabio’s old course is still useful today in order to achieve the goal of
bringing something demoscenic onto the screen reasonably quickly. Copper
effects, bitplanes, displaying IFFs, modulo effects – all this is timeless.
Didactic masterpieces should not be expected, given the fact that it was
16-20 year olds who wrote the course. The LaTeX version ought to stay that
way, too, "since we did want to preserve the original spirit of documentation
written by teenagers." Technically, the course could also do with an update,
as Thrashm One and the old Kefcon are certainly no longer at the cutting
edge. Nevertheless, it remains a clear recommendation for all beginners.

How to continue?
Amiga Assembler Buch

After Scicco had knitted together his first mini-demo based on the course, he
had to find more material from other sources. For Chunky to Planar (C2P), he
was apprenticed to Yormas/Nature and picked up topics like matrix rotation and
vector calculus from a textbook on mathematics. Even PC assembly code from the
Internet at the time helped him, for example to learn 3D routines for texture
mapping. Did that ever get really tough? "It’s all difficult, somehow, but the
things I’ve done in the last few years have been really tricky, the really rad
effects, of which you initially think: that’s not actually possible." 
Everything else, Scicco says, has only ever been new territory. So you just
have to stick with it, then it will work, somehow.

But how should one motivate oneself? Bifat/TEK clearly recommends:
"Visual programming" to me. Don’t spend too much time on theory, but put
something on the screen right away to see that you have achieved something. 
The rest comes by itself. That speaks in favour of the Corso di Assembler,
I guess.

If you can’t get any further on your own, the old tactic of learning the same
content from different sources is recommended, e.g. searching for old books or
magazines. German-speaking Amigans can download Peter Wollschlaeger’s Amiga
Assembler book as a PDF. Also of interest is "The final Assembler Book" 
("Das endliche Assemblerbuch") by Christian Ullenboom, a now successful Java
book author who could not find a publisher for his 68k course back in 1993.
As for English, the extensive tutorials by Photon/Scoopex, which he has
published on YouTube, are particularly worth mentioning. Like the Italian
course, his 43-part "Amiga Hardware Programming" series is oriented towards
beginners in scene coding.

On top of that, innumerable websites, Facebook groups and other sources from
which to get code and advice are available. Who would have thought in the
mid-90s that the Amiga scene would still be so active in 2020? Thanks to
emulators and crossassemblers like vasm, as well as the plug-in capability
of modern IDEs, it should also become increasingly simple for younger
programmers (or at least such programmers who have previously only worked
on the PC) to find their way around the Amiga universe. The Amiga scene is
looking forward to seeing you!
