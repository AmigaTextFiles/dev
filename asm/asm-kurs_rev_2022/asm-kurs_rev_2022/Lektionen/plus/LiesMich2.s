
							Warum Amiga?

oder ein Vorwort zur deutschen Übersetzung von Göran Strack (Rock'n Roll)

Die Übersetzung kommt 30 Jahre zu spät! Sicher wäre es schöner gewesen, wenn
alle die deutsche oder englische Übersetzung schon vor Jahren in den Händen 
hätten halten können, aber es war halt keiner da, der sich an die Arbeit
gemacht hat, außer Martin De Tomaso, der die ersten 7 Lektionen 1995 übersetzt
hatte. Naja, das ich dann eines Tages den kompletten "Rest" fertig übersetzen
sollte, hätte ich zu Beginn natürlich auch nicht gedacht. Aber einmal damit
angefangen, war ich irgendwie nicht mehr zu stoppen und auch mit der Qualität
der Überstzung kann man, denke ich, nun zufrieden sein, den ich spreche kein
Wort italienisch. Nur durch Maschinenübersetzungsprogramme und angeeignete
Amiga Programmierkenntnisse war es am Ende möglich.

In der Einleitung von Lektion1 steht in etwa sinngemäß: "Wie war es möglich?"
und tatsächlich ist es diese Frage gewesen, die mich antrieb. Als Amiga-
Besitzer in der Hochphase 1990-1992 war ich begeistert von der Grafik, dem
Sound usw. nur leider habe ich nie eine Zeile Assembler geschrieben, nicht
einmal Basic auch die Workbench habe ich eigentlich so gut wie nie genutzt.
Den Amiga hatte ich ausschliesslich zum Spielen eingesetzt. Natürlich war mir
klar, dass die Spiele und Demos erstmal programmiert werden müssen nur wie,
das blieb ein Rätsel.

Meinen ersten Kontakt mit einem Computer (C64) war 1986. Mein Schwager, der
etwas mehr als 10 Jahre älter ist, als ich, hat ihn irgendwie aus dem Westen
bekommen. Tatsächlich war das erste Spiel, was ich bei ihm gesehen habe Pong,
auf einem alten Scharz-Weiß-Fernseher und ich war total begeistert.
Ab diesem Augenblick war ich vom Computer-Virus infiziert, nur leider, da ich
ein Kind der DDR bin, war der Kauf eines C64 unmöglich. Später hatten auch
zwei weitere Freunde in meinem Alter einen C64, aber ich musste immer betteln
um mal wieder hinter der Kiste zu sitzen und spielen zu dürfen. Diese
Augenblicke waren gefühlt leider sehr selten, aber auch unheimliche 
Glücksmomente für mich. Geliebt habe ich Uridium, Decathlon und Grand Prix
Circuit und was hatten wir für einen Spaß bei Kickstart2, River Raid und Blue
Max...

Ich habe natürlich meine Eltern ständig (4 Jahre) genervt, dass ich mir einen
C64 wünsche. Es war leider nichts zu machen. Auch habe ich heimlich an meine
Westverwandschaft geschrieben und meinen größten Wunsch genannt, nur leider
kam auch von hier weder ein ja noch ein nein.

Meinen ersten C64 habe ich dann Weihnachten 1989 in Neuss gekauft (vom
Begrüßungsgeld! also von meinem plus dem Begrüßungsgeld von meinem Vater)
4 lange Jahre hatte ich also warten müssen...  Ich hatte dann ab Anfang 1990
einen Brieffreund aus Ratingen mit dem ich C64 Disketten getauscht habe. 
(Kennengelernt hatte ich ihn über die Zeitschrift 64'er.) Über diese
Diskettensendungen bin ich dann in meiner Heimatstadt Halberstadt auch in die
"ortsansässige" C64-Szene reingekommen und habe neue Leute kennengelernt. 
Gespräche über den Akku, Sprites, Assembler usw. haben mich dabei tief 
beeindruckt, nur zu der Zeit, konnte ich nicht mitreden...

Mein Brieffreund hat sich dann Anfang 1990 einen Amiga 500 gekauft und mir
seine alten Amiga-Magazine geschickt. Die Seiten der Magazine kannte ich
nachher auswendig. Vor allem die Spieletests und hier vor allem der Bericht
zu Rock'n Roll im Amiga-Magazin 12/1989 hatte es mir angetan.

Nun, musste ich nur noch auf den Umtauschtag 1.7.1990 warten, denn für mich
stand fest, sobald die Deutsche Mark auf dem Konto ist, also das DDR-Geld
umgetauscht ist, kaufe ich mir einen Amiga. Wir waren dann Anfang Juli 1990
noch an der Ostsee und Mitte Juli habe ich mir dann als zweiter in Halberstadt
einen Amiga gekauft. Ich behaupte einfach mal, das ich der zweite war, da ich
später "jeden" kennengelernt habe, der auch einen hatte, denn es hatte Mafia-
ähnliche Ausmaße angenommen... U.a. waren wir auch beim Sohn vom Bürgermeister,
(später war er noch Wirtschaftsminister in Sachsen-Anhalt) privat zu Hause,
um zu schauen ob es "Amiga-mäßig" etwas zu holen gibt.

Noch vor dem Ostsee-Urlaub Mitte Juli, bin ich zu einem Freund mit dem Fahrrad
gefahren, den ich in der C64-Szene beim Disketten tauschen kennengelernt hatte.
Er war der erste der einen Amiga in Halberstadt besaß und was spielt der Junge?
Rock'n Roll! Das war ja nun der Hammer! Zufälligerweise waren meine ersten
Spiele dann "Rock'n Roll, Ports of Call, Testdrive2, Xenon2". (Später hatte ich
dann ca. 500 Disketten)

Ich hatte dann natürlich täglich Rock'n Roll gespielt und irgendwann im 
September 1990 nach dem ich aus der Schule kam, habe ich es in ca. 4 bis 5
Stunden tatsächlich geschafft, das Spiel durchzuspielen! Die ganze Amigazeit
ging dann bei mir bis ungefähr Anfang 1992 und dann war der erste große Hype
bei mir erstmal durch.

Von 1992 bis März 2000 hatte ich eine Amiga- und PC-freie Zeit. Unvorstellbar,
oder? Was ist Internet? Es hat mich nicht interessiert. PC's waren für mich
klobig und langweilig.

Um es abzukürzen. Im Oktober 2000 habe ich ein Informatikstudium begonnen
(genauer Automatisierungstechnik) und zwar als absoluter PC-Analphabet. Nach
meinem Studium habe ich dann von 2005 bis 2012 keine einzige Zeile
programmiert. In diesen Jahren habe ich Schaltschränke mit Eplan konstruiert
und später technische Gebäudeausrüstung (Elektrotechnik/MSR) geplant. Erst
2012 hatte ich wieder mit dem Programmieren angefangen, zunächst mit dem
Arduino und 2015 habe ich dann zum ersten Mal seit langer Zeit meinen alten
Amiga wieder aufgebaut und eine alte Amiga Zeitschrift in der Hand gehalten.
Ich stellte mir die Frage, ob ich heute wohl verstehen würde, wie sie damals
diese Spiele und Demos programmiert haben?

Jedenfalls habe ich dann Anfang 2016 mit dem ersten youtube Tutorial von
scoopex angefangen und mich über die ersten kleinen Erfolge gefreut. Bis zum
Ende des Jahres 2016 hatte ich dann erstmal die Grundlagen, also die 68000er 
Befehle gelernt, die ersten Lektionen des dänischen Assemblerkurses
durchgearbeitet und vielleicht auch die ersten drei Lektionen des
RAMJAM-Kurses. Bis Ende 2017 war ich dann bis zum Ende der Lektion 7
vorgedrungen, ohne behaupten zu können, alles verstanden zu haben.

Jetzt wollte ich natürlich auch wissen wie der Blitter funktioniert, aber
leider fehlte dieser Teil der Übersetzung. Ich habe dann 2018 mit der
Übersetzung von Lektion 9 und 10 angefangen. Die Lektion 8 hatte ich zunächst
übersprungen. Diesen Teil der Übersetzung habe ich dann 2019 im Aminet 
veröffentlicht. Natürlich war die Übersetzung nicht perfekt, vielleicht war
sie sogar schlecht, aber sie war in jedem Fall besser als nichts. Ja, und
dann habe ich einfach immer weiter gemacht...

Dieses große Durcheinander der einzelnen Teile hat mir natürlich nicht
gefallen, sodaß ich mir dann irgendwann die Aufgabe gestellt habe, einfach
alles auf ein ordentliches Niveau zu heben. Und das ist nun das Resultat.

So und jetzt noch eine kleine Geschichte zu meinem Amigakauf. Wie ich 
geschrieben habe war mein Plan den Amiga 500 sofort zu kaufen, sobald die
Ost-Mark in die harte Währung umgetauscht war. Also nachdem wir aus dem
Ostseeurlaub (bei der wir sogar eine anstrengende Kopenhagen Reise mit Bus
und Fähre unternommen hatten) wieder zurück waren ist mein Vater mit mir
Samstags früh nach Wolfenbüttel zu foto allkauf gefahren (im Zentrum). Dort
haben wir nun den Amiga 500 (799,-) und den Monitor 1084S (849,-) gekauft.
Nun ging es zurück nach Halberstadt. Zu Hause angekommen wollte ich alles
auspacken und anschließen, aber in dem Amiga-Karton war nun das Netzteil,
der Amiga fehlte! Jetzt raste natürlich der Puls. Wir sind sofort wieder 
eingestiegen in den Trabi und was die Kiste hergab wieder durch die Dörfer.
Damals haben die Geschäfte 13:00Uhr? wieder geschlossen. Auf jeden Fall
haben wir es noch rechtzeitig geschafft. Als ich in den Laden reinkam habe
ich erstmal alle Leute zusammengeschrien. Für mich lag hier ein klarer Betrug
vor. Am Ende hat sich rausgestellt, dass der Amiga noch im Schaufenster stand.



