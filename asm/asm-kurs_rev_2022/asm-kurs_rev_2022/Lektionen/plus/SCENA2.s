 Jesus/RamJam

Il mio handle era EXECUTOR. Ho codato per anni e poi ho smesso. PERCHE?

Molte persone abbandonano il magico mondo della scena, senza motivo e poi
tornano e sentono il bisogno di essere ancora vivi.
La scena e' qualcosa di fantastico perche' ti inserisce in una struttura
grande e complessa, ti fa sentire importante ti fa conoscere molta gente e
ti da moltissime soddisfazioni.
Ma la scena "da sola" non basta per la maturazione intellettuale di una
persona. Non puoi vivere per un computer, non puoi passare le giornate a
pensare come programmare la tale routine o come realizzare la nuova musica.
La scena e' un "hobby", o meglio una "dimensione parallela", ed e' per questo
che la adoro. Ma e' "SOLO" questo, e non e' assolutamente come la vita vera,
che rimarra' sempre la cosa piu' importante per ogni essere umano.
Nella scena esiste la piu' totale liberta' di informazione e ogni persona
puo' fare virtualmente qualsiasi cosa.
Chiunque puo' diventare famoso e stimato senza dover spendere soldi ne'
conoscere politici, ma semplicemente facendo qualcosa che sara' giudicato
dagli altri, secondo il buon senso ed il gusto "popolare", "scenesco".
Ma quando si spegne il fidato computer esiste un altro mondo, forse piu'
difficile da vivere, con le sue nevrosi, i suoi problemi, ma che dobbiamo
vivere, perche' anche staccando la spina i problemi della vita rimangono.
Conosco alcune persone che vivono per la scena. La loro vita non esiste quasi
al di fuori della scena e quando le sento mi viene da chiedermi se esistano
veramente o siano solo delle allucinazioni, delle essenze virtuali.
Anche se gli si rompesse l'Amiga, loro rimarrebbero nella scena mentalmente.
Il computer e' forse uno degli hobbies piu' spettacolari, che puo' divenire
un culto quasi religioso o un modo per superare le depressioni, poiche' con un
computer puoi veramente creare qualcosa. Qualsiasi cosa.
Puoi simulare la vita che odi o la vita che vorresti, o capire le leggi che
regolano l'universo, o stupirti davanti agli infiniti che si nascondono dietro
ai frattali; ma non si tratta della realta', sono tutte simulazioni, falsita',
forse anche piu' belle della realta', o piu' brutte, piu' divertenti,...
Il computer e' uno strumento da usare nella giusta maniera. E la scena va
vissuta per quello che e', un gioco stupendo, un modo per conoscere altre
persone e passare un po' di ore in allegria. Ma non esiste solo lei.
Talvolta osservo la realta' e mi chiedo se quello che vedo esiste realmente
o se e' solo la rappresentazione di qualcosa d'altro.
Non sono mai stato convinto della unicita' della realta', a causa di un mio
difetto genetico chiamato daltonismo. Dato che io vedo la realta' con meno
colori, io non posso sapere come e' la realta' con piu' colori. E quindi
la realta' che io vedo e' una realta' esistente solo nella mia mente.
Nella scena sai gia' che quello che vedi e' falso.
Molte volte si sono lette notizie che parlavano di persone o cose che in
realta' non esistevano. Ma e' proprio questo il bello.
Uno puo' fare apparire quello che vuole ed essere quello che vuole
indipendentemente dal suo aspetto fisico o dal colore della sua pelle.
Ed ognuno puo' esprimere al meglio quello che ha dentro, quello che vuole
comunicare al mondo. Gia', perche' fare qualcosa per la scena significa anche
raggiungere molte persone in tutto il mondo con la nostra opera, a differenza
di un pittore che puo' raggiungere un solo acquirente per volta, ad esempio.
Ho una critica da muovere ai demo attuali: non comunicano niente.
Non accrescono la tua cultura o i tuoi sentimenti.

(NOTA DI RANDY: Non condivido questa affermazione di Jesus, infatti credo
  che alcune demo esprimano la visione del mondo degli autori, non in maniera
