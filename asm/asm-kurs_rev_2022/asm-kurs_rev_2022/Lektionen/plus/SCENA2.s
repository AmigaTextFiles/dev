THe RiPPeR/rj

Accetto ben volentieri la proposta dell'amico Randy/RJ di scrivere alcune
righe su un argomento così vasto, tortuoso e dannatamente affascinante quale
la "scena", sia perchè l'idea del suo corso di assembly nato e pensato da e
per la "scena" dimostra una sensibilità e intelligenza non riscontrabili in
tutti, sia perchè il poter finalmente scrivere un qualcosa, che non venga
letto solo da me stesso, nella lingua italiana, è un'occasione da non lasciar
sfuggire.
Condivido pienamente la presentazione in stile didattico-didascalico, che
poi contraddistingue tutto il corso, della "scena" fatta da Randy, e trovo
pure calzanti certe definizioni; alcuni passaggi, tuttavia, ma era
inevitabile, li definirei riduttivi e un pò fuorvianti.
Partendo quindi dalle righe scritte dallo stesso autore del corso cercherò
di proporvi l'argomento seguendo una concezione prettamente
scenico-giornalistica, se mi si permette l'orribile traduzione, piuttosto
che descrivervelo con una vision tecnica o tecnicistica proposta da Randy.
Premetto che se è pur vero che una scena PC è nata ed è in crescita è
altrettando vero che la Scena con la S maiuscola è sorta col C=64 e
continua come naturale prosecuzione sull'Amiga.
Sul perchè questo movimento abbia riscosso tale successo sulle macchine
Commodore, pur non essendo le più diffuse, è cosa interessante e di non facile
risposta.
Sono plurime le correnti di pensiero su tale argomento, ma non è questo il
luogo per soffermarci su tali problemi.
Basti sapere che ora come ora gli appartenenti alla "scena" Amiga oscillino
sull'ordine di alcune migliaia, ripartiti, come fatto giustamente notare,
in "coders", "graphicians", "musicians" e "swappers", categorie ben
illustrate precedentemente.
Fulcro, motore di tutto il sistema scenistico è il o la, a seconda si
seguano diverse scuole grammaticali, DEMO. Cosa sia un demo è già stato
abbondantemente spiegato da Randy, quello che vorrei sottolineare è che
questo fatidico demo non sia solo una sfida dell'uomo contro la macchina
(del programmatore che cerca di superare i limiti imposti dai componenti
elettronici) quanto una vera e propria, OPERA D'ARTE. 
Jesus e qualcun altro lo potranno contestare ma sono dell'idea che certe
espressioni visive, certe combinazioni di musica, colori, parole
armonicamente tessute in una sequenza viva e coinvolgente, chiamate demos,
possono essere paragonate a creazioni pittorico-architettoniche, a opere
letterarie, a vere e proprie forme d'arte. E mi sembra strano che proprio
un bravo coder come Andrea non sia mai rimasto in contemplazione di alcuni
demos, non sia mai uscito di casa, dopo aver spento il computer, arricchito
intellettualmente ed emotivamente.
Certo, un demo non può darti le medesime sensazioni che puoi avere leggendo
un romanzo, ma d'altronde, non puoi nemmeno dire che le sensazioni che hai
con una ragazza sono le stesse che si provano leggendo Dante; sono tutte
BELLE e piacevoli sensazioni, che ti arricchiscono interiormente, ma sono
DIVERSE.
Chiarito questo vorrei tornare sul concetto di internazionalità della scena,
su come siano positivamente influenti solo la personalità, il carattere e la
mentalità dello scener e non le sue appartenenze e caratteristiche sociali.
Lo scener infatti è la proiezione di una persona in una "dimensione" diversa
da quella reale.
Capisco come possa sembrare ostico questo concetto, ma ritengo sia
fondamentale sottolinearlo.
Si usa dire che una persona entra nella scena, trovandosi un soprannome,
magari creando qualcosa e iniziando a scambiare lettere e dischetti con altri
sceners, ma l'operazione è ben più complessa di come possa apparire.
Questa persona non entra nella scena, ma CREA, PLASMA un personaggio con
cui GIOCHERA' nella scena.
E questa creazione dello scener, del proprio alter ego proiettato, parte già
dalla scelta dell'alias, dal modo in cui ci si presenta ai contatti, da quello
che magari si scrive sullo scroll-text del proprio demo, accusando qualcuno
di aver usato tecniche illecite (usato la musica di qualcun altro, usato lo
scanner per fare figure) o magari esaltando sè stesso od il proprio gruppo.
E' questa la POTENZA della scena, il poter vivere esperienze diverse dal
quotidiano, il poter fare, dire, pensare, conoscere qualcosa sganciati dalla
reale condizione sociale.
E' comprensibile il timore espresso da Executor (Jesus) sul fatto che
questo sganciamento dalla realtà possa degenerare in alienazione, ma non lo
condivido. Ritengo sia un luogo comune quello nel quale lo scener medio è
un disadattato, una persona incapace di instaurare relazioni con altri
esseri umani (specie se dell'altro sesso), il ragazzino bianchiccio di
Fabio insomma (ma sei sempre abbronzato tu?? 8]). Anzi, penso che la
maggioranza degli sceners, specie la "vecchia guardia", siano dei ragazzi
che sono cresciuti assieme ai computers, che hanno visto il loro hobby, la
loro passione, trasformarsi da elitaria a divertimento di massa, che hanno
assistito a tutti questi sconvolgimenti tecnologici da una posizione più
consapevole perchè in possesso del know-how del pionierismo informatico.
E' chiaro che parlo da appartenente alla categoria e potrei essere reputato
fazioso, ma penso anche chi non ha condiviso i medesimi anni con il medesimo
spirito, se però è dotato di un minimo di coscienza critico-storica, può
perlomeno comprendere e ammettere la veridicità delle mie affermazioni.
Vorrei infine concludere questo mio breve intervento spezzando una lancia
in favore di chi nella e per la scena non fa nulla, se non diffonderne le
opere, o, molto immodestamente, scrivere pompose critiche e boriose
impressioni su ciò che gli altri producono. 
Ritengo comunque che anche questo sia un lavoro meritorio, certo, perchè se
fatto con coscienza e con un minimo di conoscenza può accrescere la
valorizzazione dell'operato dei coders, grafici e musici, rivelandone
magari oscure angolazioni od osando interpretazioni estreme del significato
del demo. Non ritengo di esagerare affermando che il giornalismo interno
alla scena ha regalato intuizioni che possono essere definite
filosoficamente illuminanti.		

