magari imparato qualcosa, ho studiato la psicologia di alcuni personaggi,
ho condiviso le idee con altri, insomma, ho cambiato qualcosa seppur minima
nella mia mente. Non mi e' mai successo qualcosa di simile vedendo una demo.

