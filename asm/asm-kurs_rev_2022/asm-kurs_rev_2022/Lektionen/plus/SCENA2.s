che ti fanno riflettere, ti identifichi in quello che vedi e trovi conforto
alla tua esistenza e alla fine ti puo' succedere di sentirti soddisfatto e
consolato, a meno che il film o video in questione non facessero schifo.
La nostra vita e' fatta di ricordi, in cui noi ci rifugiamo, in cui noi
scappiamo da quello che ci fa male in cui noi riposiamo quando siamo stanchi.
