Tra le novita' italiane, devo segnalarvi l'apertura dell'area DEMO.ITA nella
rete FIDONET. In questa area nei primi mesi non c'e' circolata un gran che
di gente, al massimo una ventina di persone, di cui l'80% PCisti, ed hanno
parlato delle routines delle linee e del texture mapping, poi anche del
progetto di fare un diskmagazine in Italiano, che pero' e' stato fatto solo
su Amiga, per ora: si tratta di INFAMIA/MHD.
Leggendo i messaggi ho notato che molti identificavano le demo con il numero
di facce dei solidi che girano a video, e altri non sapevano cosa fosse la
scena. La cosa peggiore e' che c'era disinformazione sulla scena AMIGA, data
la scarsa presenza (c'erano solo i Morbid Visions di gruppi Amiga, mentre
su PC c'erano 2 o 3 gruppi.
Per informare della situazione Amiga e delle mie idee sulle demo ho scritto
un paio di lettere, che riporto di seguito, dato che possono risultare
interessanti, almeno come "storia" dell'area DEMO.ITA:

