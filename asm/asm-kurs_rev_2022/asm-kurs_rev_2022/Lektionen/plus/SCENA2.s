			LETTERA 1
******************************************************************************
  
Salve, sono Fabio Ciucci, per la scena Randy/RAM JAM. 
Possiedo sia Amiga che PC, quindi dovrei evitare il disprezzo 
preconcetto sia dei PCisti che degli Amighisti, che ho notato 
forte qua, dato che vicino alla parola "Amiga" di solito si 
trovano cose come "schifo", "pena", ecc. 
D'altronde una componente della scena e' il tentare di mostrare, 
con le demo, che il PC e' meglio dell'Amiga o viceversa; cio' 
assieme al tentativo di dimostrare che una persona e' piu' brava 
di un'altra. 
Ma le demo sono SOLO "dimostrativi grafici", come scritto nella 
policy d'area? 
Se le demo fossero solo questo, mi sarei dato ad altro. 
E' necessario mentire a se stessi, come quando un PCista si vuol 
convincere che la bella demo amiga che sta vedendo fa schifo? 
Se un amighista parla bene del PC deve essere emarginato? 
Chi sono io? Un ragazzo ventenne, studente d'architettura. 
Nonche' coder Amiga, e un po' PC; potrei parlare di tecniche 
e routine, ma ci sono in giro .doc piu' esaurienti di me. 
Sono un veterano: le mie prime intro Amiga sono del 1990, le prime 
prove del 1986, su C64. 
In quel periodo mi chiedevo se i coder fossero umani, contemplandoli 
come divinita' o genialoidi. 
Poi, quando riuscii a fare questi dannati cubi 3d rotanti, mi resi 
conto di come fosse semplice: non serviva genialita'. 
Sbirciando nel codice, ho scoperto che molti si fregavano o 
scambiavano listati, quindi bastava ottenerli e cambiarli per 
allungare la propria demo. 
Quindi, le demo non sono fatte da geni che partono da zero e 
inventano tutto da soli, bensi' il frutto di un lavoro collettivo 
della scena. 
Di qui molti slide di effetti uguali, con oggetti e colori 
cambiati, ma routine simili, e nessun significato. 
Allora cercai di avere contatti, fondai un gruppo, poi "morto", 
e leccai i piedi ai finlandesi. 
Ma i finlandesi non rispondevano, i danesi mandavano una letterina 
di 2 righe, i tedeschi pure, gli italiani erano introvabili. 
Non ero ELITE, al loro livello: non meritavo la loro considerazione. 
La ricerca di fama nella scena spinge programmatori, grafici, ecc. 
a perdere tempo facendo demo e altro senza lucro, ne' riconoscimenti 
nel mondo reale: i soldi non contano come la fama. 
Allora mi misi a fregare routines, a cambiarle e migliorarle, e ho 
messo insieme demo "decenti", ma barando (come molti) per fare piu' 
di cio' che potevo al momento. 
MA CON LE DEMO HO OTTENUTO LA CONSIDERAZIONE DI QUALCUNO... piano 
piano divenni Elite, cominciai a leggere "Randy" nei magazine e 
nelle classifiche, e cio' mi rese ancora piu' elite. 
Racconto un fatto: tempo fa uno "nuovo" telefono' a un membro "elite" 
della scena, ma fu' trattato da pezzente, nonche' costretto a sentire 
i meriti di questo individuo. 
Conoscevo l'esaltato solo di fama, e decisi di chiamarlo pure io: 
mi tratto' rispettosamente, e addirittura mi confido' che le sue 
demo "non erano molto esaltanti". 
NOTA: Il dire di aver fatto chissa' cosa, quando la realta' e' meno 
esaltante, si dice "spandimerding". Non scherzo, il termine e' usato 
anche all'estero, (coniato pero' da un italiano: Macno). 
Poniamoci delle domande: le demo migliori sono quelle che hanno 
i solidi con piu' facce? 
Tutte le demo nuove, dato che hanno effetti momentaneamente nuovi, 
sono migliori delle demo vecchie? 
Sono piu' belle le demo Amiga o quelle PC? 
E' piu' bello il piede o la mano? 
Come si fa ad entrare nella scena? 
Un quadro impressionista, dato che e' vecchio e gli "effetti" pure, 
e' sempre peggiore di un'opera di oggi, fatta con colori 
tecnicamente superiori ed effetti innovativi? Cos'e' l'arte? 
Queste domande esprimono il concetto che, mentre la tecnica 
stupisce solo nel suo contesto storico, l'arte desta stupore 
sempre, perche' e' legata all'uomo, il cui senso artistico non muta 
nel tempo. 
L'arte si puo' creare con un pennello, con un c64, con un Amiga, con 
un 286 o con un pentium. 
Tuttora amo sentire musiche del C64, e molte le preferisco ad 
altre prodotte da schede awe32 o GUS. 
Se fai una demo lenta, e per velocizzarla compri un processore 
piu' veloce, tu rimani lo stesso, e' il computer che e' cambiato. 
Pensate al futuro: quando i computer faranno in basic quello 
che fanno ora solo in assembler, quando i programmi di ray-tracing 
calcoleranno in tempo reale a video, avra' qualche significato 
programmare demo? Le sfide si potranno dare solo su vecchie macchine: 
il concetto di demo ha come base un limite da superare, non un 
computer da aggiornare (al contrario della logica di mercato). 
Inoltre, non sarebbe bello che le demo avessero qualche senso, che 
comunicassero un messaggio? 
Immaginate una demo ambientalista, con un fungo atomico 3d in 
motion blur... 
Potresti condividere o no le idee degli autori, accrescerti 
culturalmente, anziche' stare a contare le facce e i colori di un 
solido messo li' a caso. 
Inoltre, non sarebbe bello che la gente cercasse anche demo ULTIME 
classificate, leggendo l'indirizzo degli autori, i quali 
risponderanno alla vostra lettera, al contrario dei vincitori? 
La scena non e' un modo per conoscere gente, non ancora tutta elite? 
Un nuovo gruppo non ancora bravo, dovrebbe rilasciare qualcosa come 
gli viene, scrivendo "ci scusiamo per la non alta qualita', e' la 
nostra prima intro". 
Vorrei veder uscire piu' spesso demo non eclatanti di nuovi gruppi, 
piuttosto che poche demo dei soliti gruppi elite. 
Tornando a me, dopo anni spesi stando aggiornato (ho fatto la prima 
demo italiana per a1200), mi sono reso conto che in Italia eravamo 
solo una decina di coder. 
Allora ho perso interesse per le news, e ho aiutato chi voleva 
imparare, scrivendo un corso che parte da zero assoluto: ci ho messo 
un anno, rinunciando a fare nuove demo.  
Ho fatto uscire il corso nel disco allegato ad una rivista, l'ho 
mandato nelle bbs, e ho messo annunci. 
Pensionati e dodicenni stanno imparando benissimo! 
Se avete The Games Machine n.78, (Settembre), o il n.76, noterete 
che nel talent scout si parla del mio corso. 
Se fate demo, mandatele al talent scout: cio' sensibilizza le masse! 
Su Amiga chi vuol cominciare ha solo da copiarsi il mio disco PD. 
A proposito: avvertite tutti gli amighisti in area! 
Su PC purtroppo una cosa del genere, in italiano e completa, NON 
ESISTE, come lamentano molti. 
Inoltre programmare il PC, a causa delle molte schede diverse, e' 
assai piu' difficile che programmare l'Amiga. 
So del CDROM "Demos Development Kit" e del file prg.5f8, ma sono 
solo docs sparsi, utili agli esperti, ma POCO a chi e' a zero!! 
Ho chiesto ai Soft One, ma dicono di non aver tempo per un'opera 
