			    LE DEMO COME ARTE
------------------------------------------------------------------------------
 Randy/RAM JAM

La filosofia estetica ci dice che alcune figure e alcuni suoni se combinati
in un particolare modo detto ARTISTICO producono di per se' piacere: e' il
caso di chi rimane estasiato vedendo un dipinto o ascoltando una musica; se 
ci pensate bene CHI CE LO FA FARE di provare piacere solo vedendo una figura?
Si dovrebbe provare piacere solo soddisfando i nostri desideri, ad esempio
facendo l'amore, mangiando una pizza, picchiando uno antipatico.
La psicologia infatti ci dice che fare l'amore, sfamarsi e fare
violenza, ossia sfogare aggressivita' e autoritarismo, sono le cose cui tende
una persona.
Eppure esiste UNA e UNA SOLA altra cosa che procura piacere, oltrettutto
DISINTERESSATO, ed e' l'ARTE.
Il problema cruciale e': COME MAI?
Questo lo sanno solo gli artisti! Infatti la differenza tra un artista e il
suo pubblico e' questa: entrambi traggono piacere dall'arte, ma mentre uno
spettatore non e' in grado di produrre altra arte, al massimo puo' copiarla 
perfettamente, l'artista continua a crearne, proprio perche' SA come combinare
i suoni o i colori in modo ARTISTICO, se non altro lo sa a livello intuitivo,
non lo sa spiegare precisamente, data la complessita' del concetto di ARMONIA,
ma quando e' ispirato crea ARTE.
Come per tutte le cose, anche per l'arte si possono trovare persone a cui
piace di piu' o a cui piace di meno: quelli a cui l'arte piace di piu', ossia
che si abbandonano senza problemi al piacere che essa procura in tutti gli
uomini (e donne), sono gli ARTISTI e i COLLEZIONISTI/FALSARI.
Alla seconda specie appartengono coloro che rubano immagini o suoni per
camuffare goffamente il tutto e spacciarlo per opera sua, infatti si tratta di
artisti mancati, perche' dell'artista hanno la forte attrazione per la
bellezza nelle sue svariate forme, ma gli manca il metodo, la capacita' e la
voglia di capire il PERCHE', di CERCARE LA VERITA' sul mistero dell'ARTE, cosa
che ogni artista CHE SIA ARTISTA intraprende.
L'aspetto piu' ridicolo di coloro che tagliuzzano musiche o immagini (detti in
gergo LAMER, pronuncia LEIMER), e' che non immaginando nemmeno lontanamente
la causa del piacere che gli procura un disegno o una musica, ad esempio
credono che sia la qualita' dei campionamenti audio o il numero dei colori su
schermo, ma si accorgono poi che non e' vero, basti pensare alle musiche del
Commodore64, senza campionamenti, ma spesso opere d'arte superiori a
moltissime delle musicacce Amiga.
Rimasti scontenti del fatto che aumentando i colori spesso impasticciano la
figura, o che facendo un modulo musicale lungo un megabyte, questo fa schifo
al confronto di una musichetta CHIP di 10Kb, magari del Commodore64, spesso
si comprano un PC-MSDOS credendo che cambiando il computer con quello piu' in
voga al momento, aumentino le sua capacita'.
Ma la sua fine sara' quella di aver speso milioni per avere un pentium con
tutti i fronzoli per vederci QUASI ESCLUSIVAMENTE le donne nude sul CDROM,
dato che il successo del PC+CD ROM e' SICURAMENTE dovuto al fatto che tutti
i bianchicci senza donne sbavano al pensiero di avere tante donne fatte a
mattoncini sul loro monitor.
Questo non fara' altro che chiudere definitivamente ogni speranza per costoro
di creare qualcosa di bello nella vita, e forse anche di trovare una ragazza.
Sia chiaro che il computer si puo' fare arte come schifezze, non e' vero ne'
che col computer non si puo' fare arte perche' il supporto sono i bits, ne'
che tutte quelle cavolate di ray tracing con le sferette lucide a 16 milioni
di colori che si vedono alle volte sono arte, perche' sono molto colorate o
perche' lo scanner che si e' usato era di marca.
La cosa piu' ridicola, e' che tra qualche anno i computer con processori CISC
come gli 80x86, pentium e successori saranno abbandonati, assieme ai nostri
680x0, per sistemi RISC, come il PowerPC, perche' vanno piu' veloci, e i
PCISTI MSDOSSISTI dovranno comunque buttare via quello sgabuzzino del PC.
L'arte si puo' fare con Amiga, con un pennello, con un fischietto da arbitro,
con gli stuzzicadenti, e si puo' fare con MS-DOS o con l'MSX, conta CHI usa i
mezzi, non il mezzo stesso! Non si diventa artisti usando i pennelli di
Leonardo, ne' programmatori usando il mio computer, ne' affascinanti
mettendosi le mutande di Elvis Presley.
Comunque occorre dire una cosa importante: la tecnologia si sta "evolvendo"
nel senso di processori piu' veloci e memorie di massa (CD-ROM) piu' grandi.
Questo porta a rendere piu' difficile all'utente medio la programmazione, in
quanto i processori piu' veloci (tipo RISC) sono anche piu' complicati da
programmare, inoltre andando veloci non si compete piu' sull'ottimizzazione
della routine semplice, che chiunque sa fare, ma si compete su routines
sempre piu' complesse MATEMATICAMENTE, tanto il processore riesce ad eseguire
piu' istruzioni. Di conseguenza la programmazione diverra' sempre piu' una
sottospecie della matematica, e vincera' chi la sa di piu', a differenza di
ora che vince chi conosce meglio l'hardware e il processore, cosa piu' umana.
Dal punto di vista delle memorie di massa (CD-ROM e giochi da 50MB), per
sviluppare un gioco servono ormai degli studi cinematografici, dove sono
girati dei veri e propri film digitalizzati con attrezzature costosissime, che
nessun utente medio puo' avere (nemmeno lo studio puo' avere). Quindi lo scopo
di tenersi un Amiga nel 2000 e' quello che ci si potra' ancora programmare
la demo o il gioco "in casa", con un computer di base, mentre quelli che
staranno "al passo coi tempi" saranno solo degli utilizzatori di software
di simil-realta' virtuale su cd-rom, che sara' prodotto solo da grandi
software house, e non da comuni utenti come ora, inoltre la programmazione
sara' molto difficile e ristretta agli addetti ai lavori (anche raccomandati).
Come ultima nota, occorre dire che per essere nella scena non basta essere
amico di qualcuno, ne' parlare male o bene di qualcuno della scena, ne'
conoscere la scena, ne' avere il modem e aver copiato qualche dischetto.
Per essere nella scena occorre aver fatto qualche musica, qualche
disegno o qualche routine. Altrimenti si puo' essere un trader/swapper, che
pero' sono quelli che non hanno voglia o talento di diventare scener VERI,
PRODUTTIVI, ma si limitano a dare giudizi (negativi) sulle demo, quando non
sanno giudicare ne' il codice, non sapendo programmare, ne' le musiche, ne'
la grafica, non sapendo fare niente. Costoro faranno bene a trovarsi qualche
altro svago o imparare a fare qualcosa PER la scena. Quelli che scrivono
articoli BENE, comunque, sono dei "pensatori", che influenzano potivamente
l'ambiente culturale scenistico, quindi danno il loro contributo.

