/* These defines are for the default font size.  These actually describe the
 * height of the defaults fonts.  The default font type is the topaz
 * font, which is a fixed width font that can be used in either
 * eighty-column or sixty-column mode.  The Preferences structure reflects
 * which is currently selected by the value found in the variable FontSize,
 * which may have either of the values defined below.  These values actually
 * are used to select the height of the default font.  By changing the
 * height, the resolution of the font changes as well.
 */
#define TOPAZ_EIGHTY 8
#define TOPAZ_SIXTY 9

/* Note:  Starting with V36, and continuing with each new version of
 * Intuition, an increasing number of fields of @{"struct Preferences" LINK "gg:doc/NDK/Guide/Include/intuition/preferences.h/File" 58}
 * are ignored by @{"SetPrefs()" LINK "gg:doc/NDK/Guide/intuition/SetPrefs"}.  (Some fields are obeyed only at the
 * initial @{"SetPrefs()" LINK "gg:doc/NDK/Guide/intuition/SetPrefs"}, which comes from the devs:system-configuration
 * file).  Elements are generally superseded as new hardware or software
 * features demand more information than fits in @{"struct Preferences" LINK "gg:doc/NDK/Guide/Include/intuition/preferences.h/File" 58}.
 * Parts of @{"struct Preferences" LINK "gg:doc/NDK/Guide/Include/intuition/preferences.h/File" 58} must be ignored so that applications
 * calling @{"GetPrefs()" LINK "gg:doc/NDK/Guide/intuition/GetPrefs"}, modifying some other part of @{"struct Preferences" LINK "gg:doc/NDK/Guide/Include/intuition/preferences.h/File" 58},
 * then calling @{"SetPrefs()" LINK "gg:doc/NDK/Guide/intuition/SetPrefs"}, don't end up truncating the extended
 * data.
 *
 * Consult the autodocs for @{"SetPrefs()" LINK "gg:doc/NDK/Guide/intuition/SetPrefs"} for further information as
 * to which fields are not always respected.
 */

