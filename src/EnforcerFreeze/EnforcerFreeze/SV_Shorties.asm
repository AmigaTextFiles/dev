    *****************************************************************
    *								    *
    *	SV_Shorties.ASM 					    *
    *								    *
    *	    Some simple Routines to be executed in Supervisormode   *
    *								    *
    *****************************************************************

	XDEF _a7d0
	XDEF @a7d0
	XDEF _vbrd0
	XDEF @vbrd0
	XDEF _rte
	XDEF @rte

	SECTION text,code

    *****************************************************************
    *								    *
    *	    Get the current Position of the Supervisorstack	    *
    *								    *
    *****************************************************************
			    *
    _a7d0:		    *
    @a7d0:		    *
	MOVE.L A7,D0	    *
	RTE		    *
			    *
    *****************************************************************
    *								    *
    *	    Get the VBR 					    *
    *								    *
    *****************************************************************
			    *
    _vbrd0:		    *
    @vbrd0:		    *
	MOVEC.L VBR,D0	    *
	RTE		    *
			    *
    *****************************************************************
    *								    *
    *	 Restore the Supervisorstack and leave Supervisor mode	    *
    *								    *
    *****************************************************************
			    *
    _rte:		    *
	MOVE.L $4(A7),A0    *
    @rte:		    *
	MOVE.L A0,A7	    *
	RTE		    *
			    *
    *****************************************************************

	END

