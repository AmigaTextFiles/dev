// FUNCTIONS //////////////////////////////////////////////////////////////////

keyboard::keyboard()
{
	keyBuffer=NULL;

#ifdef EVERSION__UNICODE
	::SDL_EnableUNICODE(1);
#endif

	buttons[a] = SDLK_z;
	buttons[b] = SDLK_x;
	buttons[start] = SDLK_RETURN;
	buttons[select] = SDLK_SPACE;

	buttons[up] = SDLK_UP;
	buttons[down] = SDLK_DOWN;
	buttons[left] = SDLK_LEFT;
	buttons[right] = SDLK_RIGHT;

	update();
}

keyboard::~keyboard()
{
}

